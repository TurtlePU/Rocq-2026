    и использовать конструктор [ReflectT].  Если же мы применим [inversion], то
    мы извлечём свидетельство в пользу [P] из доказательства [reflect P true].

    Аналогично, единственный способ показать, что [reflect P false] -- поместить
    свидетельство [~ P] в конструктор [ReflectF]. *)

