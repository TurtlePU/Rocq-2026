(** ** Пример: Рефлексивно-транзитивное замыкание *)

(** Вот ещё один пример: _рефлексивно-транзитивное замыкание_ отношения [R] --
    наименьшее отношение, содержащее [R], которое и рефлексивно, и транзитивно.
    Его можно построить с помощью следующих трёх правил (в отличие от
    [clos_trans], мы добавили правило, постулирующее рефлексивность):

                        R x y
                --------------------- (rt_step)
                clos_refl_trans R x y

                --------------------- (rt_refl)
                clos_refl_trans R x x

     clos_refl_trans R x y    clos_refl_trans R y z
     ---------------------------------------------- (rt_trans)
                clos_refl_trans R x z
*)

Inductive clos_refl_trans {X: Type} (R: X->X->Prop) : X->X->Prop :=
  | rt_step (x y : X) :
      R x y ->
      clos_refl_trans R x y
  | rt_refl (x : X) :
      clos_refl_trans R x x
  | rt_trans (x y z : X) :
      clos_refl_trans R x y ->
      clos_refl_trans R y z ->
      clos_refl_trans R x z.

(** Кстати, это позволяет нам дать эквивалентное определение гипотезы Коллатца.
    Сначала выпишем функцию шага Коллатца в виде отношения: *)

Definition cs (n m : nat) : Prop := csf n = m.

(** Это "отношение шага Коллатца" можно подставить в рефлексивно-транзитивное
    замыкание, чтобы получить _многошаговое отношение Коллатца_, выражающее, что
    число [n] достигает другого числа [m] за некоторое число шагов Коллатца: *)

Definition cms n m := clos_refl_trans cs n m.
Conjecture collatz' : forall n, n <> 0 -> cms n 1.

(** Полученное отношение [cms], определённое через [clos_refl_trans],
    допускает более интересные выводы, чем линейные выводы наивно определённого
    отношения [Collatz_holds_for]:

csf 16 = 8         csf 8 = 4         csf 4 = 2         csf 2 = 1
————————(rt_step)  ———————(rt_step)  ———————(rt_step)  ———————(rt_step)
cms 16 8           cms 8 4           cms 4 2           cms 2 1
—————————————————————————(rt_trans)  ————————————————————————(rt_trans)
        cms 16 4                              cms 4 1
        —————————————————————————————————————————————(rt_trans)
                           cms 16 1
*)

