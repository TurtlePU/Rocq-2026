(** ** Разбор и инверсия свидетельств *)

(** Пусть мы доказываем некоторый факт о числе [n], и среди гипотез у нас
    есть [ev n].  Мы уже знаем, как разбирать случаи по [n] с помощью [destruct]
    и [induction], генерируя разные подцели для случая, когда [n = O], и когда
    [n = S n'] для некоторого [n'].  Но для некоторых доказательств мы можем
    вместо этого захотеть _напрямую_ проанализировать свидетельство [ev n].

    В качестве инструмента для таких доказательств мы можем формализовать
    интуитивную характеризацию свидетельств [ev n], которую мы проделали выше,
    используя [destruct]. *)

Lemma ev_inversion : forall (n : nat),
    ev n ->
    (n = 0) \/ (exists n', n = S (S n') /\ ev n').
Proof.
  intros n E.  destruct E as [ | n' E'] eqn:EE.
  - (* E = ev_0 : ev 0 *)
    left. reflexivity.
  - (* E = ev_SS n' E' : ev (S (S n')) *)
    right. exists n'. split. reflexivity. apply E'.
Qed.

(** Факты вроде этого часто называют "леммами об инверсии", поскольку они
    позволяют нам как бы "развернуть вспять" некоторую информацию, чтобы
    рассуждать о всевозможных способах того, каким образом эта информация могла
    быть получена.

    Есть два способа получить [ev n], и лемма об инверсии это показывает
    явно. *)

(** **** Упражнение: 1 звезда, стандартное (le_inversion)

    Докажите похожую лемму об инверсии для [le]. *)
