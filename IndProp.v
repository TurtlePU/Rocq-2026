(** ** Пример: Транзитивное замыкание *)

(** Другой пример: _транзитивное замыкание_ отношения [R] -- это наименьшее
    отношение, содержащее [R], являющееся транзитивным. Его можно задать
    следующими правилами:

                     R x y
                ---------------- (t_step)
                clos_trans R x y

       clos_trans R x y    clos_trans R y z
       ------------------------------------ (t_trans)
                clos_trans R x z

    В Rocq это выглядит вот так:
*)

Inductive clos_trans {X: Type} (R: X->X->Prop) : X->X->Prop :=
  | t_step (x y : X) :
      R x y ->
      clos_trans R x y
  | t_trans (x y z : X) :
      clos_trans R x y ->
      clos_trans R y z ->
      clos_trans R x z.

(** Так, пусть у нас определено отношение "является родителем"... *)

Inductive Person : Type := Sage | Cleo | Ridley | Moss.

Inductive parent_of : Person -> Person -> Prop :=
  po_SC : parent_of Sage Cleo
| po_SR : parent_of Sage Ridley
| po_CM : parent_of Cleo Moss.

(** Здесь, [Sage] родитель [Cleo] и [Ridley], а [Cleo] -- родитель [Moss].
*)

(** Отношение [parent_of] не транзитивно, но мы можем определить отношение
    "быть предком для" как транзитивное замыкание этого отношения: *)

Definition ancestor_of : Person -> Person -> Prop :=
  clos_trans parent_of.

(** Вот дерево вывода, демонстрирующее, что Sage -- предок Moss:

 ———————————————————(po_SC)     ———————————————————(po_CM)
 parent_of Sage Cleo            parent_of Cleo Moss
—————————————————————(t_step)  —————————————————————(t_step)
ancestor_of Sage Cleo          ancestor_of Cleo Moss
————————————————————————————————————————————————————(t_trans)
                ancestor_of Sage Moss
*)

Example ancestor_of_ex : ancestor_of Sage Moss.
Proof.
  unfold ancestor_of. apply t_trans with Cleo.
  - apply t_step. apply po_SC.
  - apply t_step. apply po_CM. Qed.

(** Вычисление транзитивного замыкания [R] может оказаться неразрешимым
    даже для разрешимого [R] (например, см. отношение [cms] ниже), так что
    в общем случае мы не можем ожидать, что транзитивное замыкание определимо
    через булеву функцию. К счастью, Rocq позволяет нам определить транзитивное
    замыкание в качестве индуктивного отношения.

    Также заметим, что транзитивное замыкание бинарного отношения в общем случае
    не выражается в логике первого порядка! Однако логика Rocq гораздо мощнее,
    так что в ней такие индуктивные отношения определяются с лёгкостью. *)

