(** ** Пример: Перестановки *)

(** Другое знакомое понятие _перестановки_ также имеет элегантную формулировку
    в виде индуктивного отношения.  Для простоты давайте сфокусируемся на
    перестановках списков длины 3.

    Мы можем определить такие перестановки с помощью следующих правил:

               --------------------- (perm3_swap12)
               Perm3 [a;b;c] [b;a;c]

               --------------------- (perm3_swap23)
               Perm3 [a;b;c] [a;c;b]

            Perm3 l1 l2       Perm3 l2 l3
            ----------------------------- (perm3_trans)
                     Perm3 l1 l3

    Так, мы можем получить [Perm3 [1;2;3] [3;2;1]] следующим образом:

    ————————(perm_swap12)  —————————————————————(perm_swap23)
    Perm3 [1;2;3] [2;1;3]  Perm3 [2;1;3] [2;3;1]
    ——————————————————————————————(perm_trans)  ————————————(perm_swap12)
        Perm3 [1;2;3] [2;3;1]                   Perm [2;3;1] [3;2;1]
        —————————————————————————————————————————————————————(perm_trans)
                          Perm3 [1;2;3] [3;2;1]
*)

(** Это определение утверждает следующее:
      - Если [l2] можно получить из [l1], поменяв местами первый и второй
        элемент, то [l2] -- перестановка [l1].
      - Если [l2] можно получить из [l1], поменяв местами второй и третий
        элемент, то [l2] -- перестановка [l1].
      - Если [l2] -- перестановка [l1], а [l3] -- перестановка [l2], то [l3] --
        перестановка [l1]. *)

(** В Rocq, [Perm3] получает следующее индуктивное определение: *)

Inductive Perm3 {X : Type} : list X -> list X -> Prop :=
  | perm3_swap12 (a b c : X) :
      Perm3 [a;b;c] [b;a;c]
  | perm3_swap23 (a b c : X) :
      Perm3 [a;b;c] [a;c;b]
  | perm3_trans (l1 l2 l3 : list X) :
      Perm3 l1 l2 -> Perm3 l2 l3 -> Perm3 l1 l3.

