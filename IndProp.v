    чтобы генерировать гипотезу [(n =? m) = true], которое требует некоторого
    причёсывания перед использованием, улучшенный способ сразу даст нужное нам
    утверждение: [n = m].

    Следуя терминологии, введённой в [Logic], мы называем это "принципом
    рефлексии на равенстве чисел", и мы говорим, что булево значение [n =? m]
