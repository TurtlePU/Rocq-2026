(** Мы можем использовать лемму об инверсии, которую мы доказали выше, чтобы
    лучше структурировать доказательства: *)

Theorem evSS_ev : forall n, ev (S (S n)) -> ev n.
Proof.
  intros n E. apply ev_inversion in E. destruct E as [H0|H1].
  - discriminate H0.
  - destruct H1 as [n' [Hnn' E']]. injection Hnn' as Hnn'.
    rewrite Hnn'. apply E'.
Qed.

(** Обратите внимание, как лемма об инверсии порождает две подцели, где
    каждая соответствует одному из способов доказать [ev].  Первая подцель
    противоречива и снимается с помощью [discriminate].  Вторая подцель
    использует [injection] и [rewrite].

    Rocq предоставляет тактику [inversion], которая воплощает этот паттерн,
    чтобы нам не пришлось явно формулировать и доказывать лемму об инверсии
    для каждого [Индуктивного] определения.

    Кроме этого, тактика [inversion] может заметить, что (1) первый случай,
    [n = 0], неприменим, и (2) [n'], возникающий в случае [ev_SS], совпадает с
    [n].  Также как и [destruct], в [inversion] есть аннотация [as], позволяющая
    нам выдавать имена вместо сгенерированных Rocq. *)

Theorem evSS_ev' : forall n,
  ev (S (S n)) -> ev n.
Proof.
  intros n E.  inversion E as [| n' E' Hnn'].
  (* Здесь мы уже разбираем случай [E = ev_SS n' E']. *)
  apply E'.
Qed.

(** Тактика [inversion] может применять принцип взрыва к "очевидно
    противоречивым" гипотезам, содержащим индуктивно определённые свойства. При
    работе с леммой об инверсии напрямую это потребует немного больше работы.
    Сравним: *)

Theorem one_not_even : ~ ev 1.
Proof.
  intros H. apply ev_inversion in H.  destruct H as [ | [m [Hm _]]].
  - discriminate H.
  - discriminate Hm.
Qed.

Theorem one_not_even' : ~ ev 1.
Proof. intros H. inversion H. Qed.

