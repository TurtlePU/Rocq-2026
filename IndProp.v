(** В главе [Logic] мы увидели, что иногда нам нужно связывать булевы
    вычисления с утверждениями в [Prop].  Но выполнение этой связи тем способом,
    которым мы это делали, приводит к рутинным преобразованиям в скриптах
    доказательств.  Рассмотрим доказательство следующей теоремы: *)

Theorem filter_not_empty_In : forall n l,
  filter (fun x => n =? x) l <> [] -> In n l.
Proof.
  intros n l. induction l as [|m l' IHl'].
  - (* l = nil *)
    simpl. intros H. apply H. reflexivity.
  - (* l = m :: l' *)
    simpl. destruct (n =? m) eqn:H.
    + (* n =? m = true *)
      intros _. rewrite eqb_eq in H. rewrite H.
      left. reflexivity.
    + (* n =? m = false *)
      intros H'. right. apply IHl'. apply H'.
Qed.

(** В первой ветке после [destruct], мы явно применяем лемму [eqb_eq]
    к уравнению, сгенерированному разбором случаев [n =? m], чтобы преобразовать
    гипотезу [n =? m = true] в [n = m]; затем нам нужно было [переписать]
    по этому равенству, чтобы завершить разбор случая. *)

