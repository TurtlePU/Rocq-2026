Check ev_SS : forall (n : nat), ev n -> ev (S (S n)).

(** Действительно, Rocq принимает и такое определение [ev]: *)

Module EvPlayground.

Inductive ev : nat -> Prop :=
  | ev_0  : ev 0
  | ev_SS : forall (n : nat), ev n -> ev (S (S n)).

End EvPlayground.

(** Про эти конструкторы можно думать как про "простейшие свидетельства
    чётности", которыми можно пользоваться как доказанными теоремами.
    В частности, мы можем использовать тактику [apply] с этими конструкторами,
    чтобы получить свидетельства чётности конкретных чисел... *)

Theorem ev_4 : ev 4.
Proof. apply ev_SS. apply ev_SS. apply ev_0. Qed.

