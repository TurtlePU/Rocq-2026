(** ** Пример: Чётность (снова) *)

(** Мы видели как минимум два способа формализации понятия чётности:

      (1) [even n = true] (используя рекурсивную функцию [even]), или

      (2) [exists k, n = double k] (используя квантор существования). *)

(** Третий способ, который мы будем использовать в качестве подопытного кролика
    в этой главе, это сказать, что число чётное, если мы можем _установить_ его
    чётность, пользуясь следующими двумя правилами вывода:

                          ---- (ev_0)
                          ev 0

                          ev n
                      ------------ (ev_SS)
                      ev (S (S n))
*)

(** Другими словами,
       - Число [0] чётно.
       - Если [n] чётно, то [S (S n)] тоже чётно. *)

(** (Конечно, определять чётность таким образом странно, поскольку у нас
    уже было два прекрасных рабочих определения. Однако это хороший "подопытный
    кролик", поскольку его определение простое и компактное, а к более
    убедительным примерам выше мы вернёмся позднее.) *)

(** Вот как с помощью этих правил доказывать чётность четырёх:

                           ———— (ev_0)
                           ev 0
                       ———————————— (ev_SS)
                       ev (S (S 0))
                   ———————————————————— (ev_SS)
                   ev (S (S (S (S 0))))
*)

(** Другими словами, чтобы показать, что [4] чётно, по правилу [ev_SS]
    достаточно показать, что [2] чётно. Тогда, в свою очередь, вновь по правилу
    [ev_SS], нам достаточно показать, что [0] чётно. Но этот факт уже напрямую
    следует из правила [ev_0]. *)

(** Мы можем перевести "бумажное" определение чётности выше в [Индуктивное]
    объявление, где каждая "возможность числа оказаться чётным" соответствует
    своему собственному конструктору: *)

Inductive ev : nat -> Prop :=
  | ev_0                       : ev 0
  | ev_SS (n : nat) (H : ev n) : ev (S (S n)).

(** Такие определения интересным образом отличаются от предыдущих [Индуктивных]
    определений вроде [nat] и [list]. Во-первых, мы определяем не [Type]
    (как [nat]), и даже не функцию, возвращающую [Type] (как [list]), а функцию
    из [nat] в [Prop] -- то есть, свойство натуральных чисел. Но что
    по-настоящему ново: поскольку аргумент типа [nat] в [ev] появляется _справа_
    от двоеточия в первой строчке, ему позволено принимать _разные_ значения в
    результирующих типах конструкторов: [0] в типе [ev_0] и [S (S n)] в типе
    [ev_SS]. Соответственно, тип каждого конструктора должен быть указан явно
    (после двоеточия), и должен иметь вид [ev n] для некоторого [n].

    Для сравнения, вспомним определение [list]:

    Inductive list (X:Type) : Type :=
      | nil
      | cons (x : X) (l : list X).

    либо (эквивалентно, но более явно):

    Inductive list (X:Type) : Type :=
      | nil                       : list X
      | cons (x : X) (l : list X) : list X.

   Это определение вводит параметр [X] _глобально_, _слева_ от двоеточия,
   вынуждая результаты [nil] и [cons] иметь один и тот же тип (здесь --
   [list X]).  Если бы мы попытались сделать то же самое в определении [ev],
   передвинув [nat] налево от двоеточия, мы бы увидели ошибку: *)

Fail Inductive wrong_ev (n : nat) : Prop :=
  | wrong_ev_0 : wrong_ev 0
  | wrong_ev_SS (H: wrong_ev n) : wrong_ev (S (S n)).
