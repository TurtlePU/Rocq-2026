(** Поведение [индукции] по свидетельствам -- такое же, как на данных:
    Rocq генерирует по подцели на конструктор, который мог быть использован для
    построения этого свидетельства, предоставляя предположение индукции
    для каждого рекурсивного вхождения свидетельства в себя.

    Чтобы доказать, что некоторое свойство [P n] выполнено для всех чётных [n]
    (т.е. для тех, для которых [ev n] выполнено), мы можем воспользоваться
    индукцией по [ev n]. Это потребует от нас доказательства двух вещей,
    соответствующих двум способам, которым свидетельство [ev n] могло быть
    построено. Если был использован [ev_0], то [n=0] и должно быть выполнено
    [P 0]. Если был использован [ev_SS], то свидетельство [ev n] имеет вид
    [ev_SS n' E'], где [n = S (S n')], а [E'] -- свидетельство [ev n']. В этом
    случае предположение гипотезы гласит, что выполнено [P n']. *)

(** Вновь попробуем доказать эту лемму: *)

Lemma ev_Even : forall n,
  ev n -> Even n.
Proof.
  unfold Even. intros n E.
  induction E as [|n' E' IH].
  - (* E = ev_0 *)
    exists 0. reflexivity.
  - (* E = ev_SS n' E',  with IH : Even n' *)
    destruct IH as [k Hk]. rewrite Hk.
    exists (S k). simpl. reflexivity.
Qed.

(** Здесь мы видим, что Rocq сгенерировал [IH], соответствующую [E'],
    единственному рекурсивному вхождению [ev] в его собственное определение. Раз
    [E'] упоминает [n'], то предположение индукции говорит об [n'], а не об [n]
    или каком-то другом числе. *)

(** Из этого следует эквивалентность второго и третьего определений чётности. *)

Theorem ev_Even_iff : forall n,
  ev n <-> Even n.
Proof.
  intros n. split.
  - (* -> *) apply ev_Even.
  - (* <- *) unfold Even. intros [k Hk]. rewrite Hk. apply ev_double.
Qed.

