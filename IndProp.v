Theorem inversion_ex1 : forall (n m o : nat),
  [n; m] = [o; o] -> [n] = [m].
Proof.
  intros n m o H. inversion H. reflexivity. Qed.

Theorem inversion_ex2 : forall (n : nat),
  S n = O -> 2 + 2 = 5.
Proof.
  intros n contra. inversion contra. Qed.

(** Вот как тактика [inversion] работает в целом.
      - Пусть имя [H] относится к предположению [P] в текущем контексте, где [P]
        было определено с помощью объявления [Inductive].
      - Для каждого из конструкторов [P], [inversion H] генерирует по подцели,
        где [H] заменяется конкретным набором условий, благодаря которым этот
        конструктор мог быть использован для доказательства [P].
      - Некоторые из этих подцелей могут быть противоречащими сами себе;
        [inversion] их выкидывает.
      - Оставшиеся подцели должны быть доказаны для доказательства исходной
        цели. В них [inversion] добавляет все равенства, которые должны
        выполняться на аргументах [P] -- например, [n' = n] в доказательстве
        [evSS_ev]. *)

