(** ** Пример: Сравнение чисел *)

(** Бинарное _отношение_ на множестве [X] в Rocq можно представить как функцию
    [X -> X -> Prop]. Другими словами, отношение -- это семейство утверждений,
    параметризованное парой значений типа [X], т.е. утверждение о парах [X]. *)

(** Например, уже знакомое Вам отношение меньше-либо-равно на [nat] имеет тип
    [nat -> nat -> Prop], и его можно определить индуктивно следующим образом:
*)

(**

                           ------ (le_n)
                           le n n

                           le n m
                         ---------- (le_S)
                         le n (S m)
*)
(** Эти правила постулируют, что есть два способа доказать, что одно число
    меньше либо равно другого: либо заметить, что это одно и то же число, либо,
    если большее число имеет вид [S m], показать, что меньшее число меньше либо
    равно [m]. *)

(** Эти рассуждения соответствуют следующему определению в Rocq: *)

Module LePlayground.

Inductive le : nat -> nat -> Prop :=
  | le_n (n : nat)   : le n n
  | le_S (n m : nat) : le n m -> le n (S m).

Notation "n <= m" := (le n m) (at level 70).

(** Это определение немного проще и элегантнее, чем наше определение булевой
    функции [leb] из [Basics]. Но, как всегда, [le] и [leb] эквивалентны,
    и об этом у нас обязательно будет упражнение. *)

Example le_3_5 : 3 <= 5.
Proof.
  apply le_S. apply le_S. apply le_n. Qed.

End LePlayground.

