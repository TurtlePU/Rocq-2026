(* ================================================================= *)
(** ** Пример: Гипотеза Коллатца *)

(** _Гипотеза Коллатца_ -- знаменитая нерешённая проблема теории чисел.

    У неё достаточно простая формулировка.  Во-первых, определим функцию шага
    Коллатца на числах: *)

Fixpoint div2 (n : nat) : nat :=
  match n with
    0 => 0
  | 1 => 0
  | S (S n) => S (div2 n)
  end.

Definition csf (n : nat) : nat :=
  if even n then div2 n
  else (3 * n) + 1.

(** Давайте теперь посмотрим, что получится, если снова и снова применять [csf]
    к некоторому начальному значению.  Например, [csf 12] равно [6]; [csf 6] --
    трём и, продолжая таким образом, мы получаем последовательность
    [12, 6, 3, 10, 5, 16, 8, 4, 2, 1].

    Если же мы начнём с [19], то последовательность получится длиннее:
    [19, 58, 29, 88, 44, 22, 11, 34, 17, 52, 26, 13, 40, 20, 10, 5, 16, 8, 4, 2, 1].

    Обе эти последовательности рано или поздно достигают [1].  Вопрос,
    поставленный Коллатцем, следующий: _Любая_ ли последовательность,
    начинающаяся с положительного натурального числа, достигает [1]? *)

(** Чтобы формализовать этот вопрос в Rocq, мы могли бы попытаться определить
    рекурсивную _функцию_, вычисляющую число шагов, которое требуется некоторой
    последовательности, чтобы достигнуть [1]. *)

Fail Fixpoint reaches1_in (n : nat) : nat :=
  if n =? 1 then 0
  else 1 + reaches1_in (csf n).

(** В обычном языке программирования такое определение сработает. Однако
    проверка завершимости Rocq-а такое определение отвергнет, поскольку аргумент
    рекурсивного вызова, равный [csf n], не "очевидно меньше" чем [n].

    Напомним, это ограничение не случайно: функции в Rocq должны завершаться на
    всех входах, чтобы система была логически непротиворечива.

    Казалось бы, может, сделать проверку завершимости умнее? Но решение того,
    тотальна ли [reaches1_in], было бы эквивалентно решению гипотезы Коллатца!
*)

(** С другой стороны, можно попробовать выразить понятие "достигает [1]
    в последовательности Коллатца" как _рекурсивно_ определённое свойство чисел.
*)

Fail Fixpoint Collatz_holds_for (n : nat) : Prop :=
  match n with
  | 0 => False
  | 1 => True
  | _ => if even n then Collatz_holds_for (div2 n)
                   else Collatz_holds_for ((3 * n) + 1)
  end.

(** Но такое определение также не проходит проверку завершимости: хоть
    теоретически мы и могли бы убедить Rocq, что [div2 n] меньше [n], мы
    совершенно точно не сможем сказать, что [(3 * n) + 1] меньше [n]! *)

(** К счастью, есть другой способ это сделать: мы можем выразить понятие
    "достигает [1] в последовательности Коллатца" как _индуктивно_ определённое
    свойство. В математике этот способ называется
    "задание с помощью правил вывода":

                  ------------------- (Chf_one)
                  Collatz_holds_for 1

     even n = true      Collatz_holds_for (div2 n)
     --------------------------------------------- (Chf_even)
                     Collatz_holds_for n

     even n = false    Collatz_holds_for ((3 * n) + 1)
     ------------------------------------------------- (Chf_odd)
                    Collatz_holds_for n

    Другими словами, у нас есть три способа доказать, что [n] в конечном итоге
    достигает 1 в последовательности Коллатца:
        - [n] уже равен 1;
        - [n] чётно, а [div2 n] в конечном итоге достигает 1;
        - [n] нечётно, а [(3 * n) + 1] в конечном итоге достигает 1.
*)
(** Мы можем доказать, что число достигает 1, построив (конечное) дерево вывода
    с использованием этих правил. Например, вот вывод того факта, что 12
    достигает 1 (где мы опустили утверждения о чётности/нечётности):

                    ———————————————————— (Chf_one)
                    Collatz_holds_for 1
                    ———————————————————— (Chf_even)
                    Collatz_holds_for 2
                    ———————————————————— (Chf_even)
                    Collatz_holds_for 4
                    ———————————————————— (Chf_even)
                    Collatz_holds_for 8
                    ———————————————————— (Chf_even)
                    Collatz_holds_for 16
                    ———————————————————— (Chf_odd)
                    Collatz_holds_for 5
                    ———————————————————— (Chf_even)
                    Collatz_holds_for 10
                    ———————————————————— (Chf_odd)
                    Collatz_holds_for 3
                    ———————————————————— (Chf_even)
                    Collatz_holds_for 6
                    ———————————————————— (Chf_even)
                    Collatz_holds_for 12
*)

(** В Rocq этому соответствует _индуктивное определения свойства_: *)

Inductive Collatz_holds_for : nat -> Prop :=
  | Chf_one : Collatz_holds_for 1
  | Chf_even (n : nat) : even n = true ->
                         Collatz_holds_for (div2 n) ->
                         Collatz_holds_for n
  | Chf_odd (n : nat) :  even n = false ->
                         Collatz_holds_for ((3 * n) + 1) ->
                         Collatz_holds_for n.

(** Здесь мы использовали механизм [Индуктивных] определений Rocq, чтобы
    характеризовать свойство "гипотеза Коллатца верна для...", описав три
    способа, которым мы можем доказать, что оно выполнено: (1) Коллатц выполнен
    для [1], (2) если Коллатц выполнен для [div2 n] и [n] чётно, то Коллатц
    выполнен для [n], и (3) если Коллатц выполнен для [(3 * n) + 1]
    и [n] нечётно, то Коллатц выполнен для [n]. Это определение в Rocq напрямую
    соответствует трём "правилам вывода", сформулированным выше. *)

(** Теперь мы можем доказывать, что последовательность Коллатца, начинающаяся в
    некотором конкретном [n], достигает 1: *)

Example Collatz_holds_for_12 : Collatz_holds_for 12.
Proof.
  apply Chf_even. reflexivity. simpl.
  apply Chf_even. reflexivity. simpl.
  apply Chf_odd. reflexivity. simpl.
  apply Chf_even. reflexivity. simpl.
  apply Chf_odd. reflexivity. simpl.
  apply Chf_even. reflexivity. simpl.
  apply Chf_even. reflexivity. simpl.
  apply Chf_even. reflexivity. simpl.
  apply Chf_even. reflexivity. simpl.
  apply Chf_one.
Qed.

(** Гипотеза Коллатца же, в свою очередь, утверждает, что _любая_
    последовательность Коллатца достигает [1]: *)

Conjecture collatz : forall n, n <> 0 -> Collatz_holds_for n.

(** Если у Вас получится доказать эту гипотезу, у Вас светлое будущее на поприще
    теории чисел!  Но не тратьте на неё слишком много времени -- эта гипотеза
    открыта с 1937. *)

