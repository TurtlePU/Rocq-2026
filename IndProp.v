(** Аргументы слева от двоеточия в [Индуктивном] определении называют
    "параметрами", а справа -- "индексами" или "аннотациями".

    Например, [X] в [Inductive list (X : Type) := ...] -- параметр, а безымянный
    аргумент типа [nat] в [Inductive ev : nat -> Prop := ...] -- индекс. *)

(** Про определение [ev] можно думать как про определение свойства
    [ev : nat -> Prop] вместе с двумя "конструкторами свидетельств": *)

