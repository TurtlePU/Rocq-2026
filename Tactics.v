(** * Применение [destruct] к составным выражениям *)

(** Мы увидели много примеров, где [destruct] используется для разбора
    случаев по значению некоторой переменной.  Однако иногда нам может
    потребоваться разбирать случаи по значению некоторого _выражения_.
    [destruct] позволяет делать и такое.

    Вот парочка примеров: *)

Definition sillyfun (n : nat) : bool :=
  if n =? 3 then false
  else if n =? 5 then false
  else false.

Theorem sillyfun_false : forall (n : nat),
  sillyfun n = false.
Proof.
  intros n. unfold sillyfun.
  destruct (n =? 3) eqn:E1.
    - (* n =? 3 = true *) reflexivity.
    - (* n =? 3 = false *) destruct (n =? 5) eqn:E2.
      + (* n =? 5 = true *) reflexivity.
      + (* n =? 5 = false *) reflexivity.  Qed.

(** Раскрыв [sillyfun] в доказательстве выше, мы видим, что застряли на
