(** * Применение тактик к гипотезам *)

(** По умолчанию, большинство тактик работают с целью, и оставляют
    контекст без изменений.  Однако у многих из них есть вариация, применяющая
    двойственную операцию к утверждению в контексте.

    Например, тактика "[simpl in H]" упрощает гипотезу [H] из контекста. *)

Theorem S_inj : forall (n m : nat) (b : bool),
  ((S n) =? (S m)) = b  ->
  (n =? m) = b.
Proof.
  intros n m b H. simpl in H. apply H.  Qed.

(** Похожим образом, [apply L in H] сопоставляет некоторое условное
    утверждение [L] (допустим, вида [X -> Y]) с гипотезой [H] в контексте.
    Однако, в отличие от обычного [apply] (который заменяет цель, сопоставимую
    с [Y], на подцель [X]), [apply L in H] сопоставляет [H] с [X] и в случае
    успеха заменяет её на [Y].

    Другими словами, [apply L in H] позволяет нам производить "прямой ход
    рассуждений": если верно [X -> Y], а также верно утверждение, сопоставимое
    с [X], то верно также и утверждение, сопоставимое с [Y].

    Для сравнения, [apply L] это "обратный ход рассуждений": он говорит, что
    если мы знаем, что [X -> Y], и пытаемся доказать [Y], тогда нам достаточно
    доказать [X].

    Вот вариант доказательства выше, использующий прямой ход рассуждений вместо
    обратного. *)

Theorem silly4 : forall (n m p q : nat),
  (n = m -> p = q) ->
  m = n ->
  q = p.
Proof.
  intros n m p q EQ H.
  symmetry in H. apply EQ in H. symmetry in H.
  apply H.  Qed.

(** Прямой ход рассуждений начинает с того, что _дано_ (допущения и ранее
    доказанные теоремы) и постепенно накапливает новые факты до тех пор, пока
    цель не будет достигнуто.  Обратный ход рассуждений начинает с _цели_,
    и пытается выяснить, из чего будет следовать цель, до тех пор, пока не будут
    достигнуты либо изначальные допущения, либо ранее доказанные теоремы.

    Неформальные математические доказательства, как правило, используют прямой
    ход рассуждений.  В свою очередь, идиоматичное использование Rocq
    подразумевает обратный ход рассуждений; но, конечно, в некоторых ситуациях
    в прямом ходе рассуждений проще разобраться. *)

