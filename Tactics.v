(** Теперь мы обсудили многие из наиболее фундаментальных тактик Rocq.
    В грядущих главах мы введём ещё парочку, а позднее увидим несколько мощных
    тактик для _автоматики_, благодаря которым Rocq сможет помочь нам
    с низкоуровневыми деталями.  Но в целом у нас уже есть всё, чтобы доказывать
    любые утверждения.

    Повторимся, какие тактики мы уже видели:

      - [intros]: перенести гипотезы/переменные из цели в контекст

      - [reflexivity]: закончить доказательство (когда цель имеет вид [e = e])

      - [apply]: доказать цель с помощью гипотезы, леммы или конструктора

      - [apply... in H]: применить гипотезу, лемму или конструктор к гипотезе
        в контексте (прямой ход рассуждений)

      - [apply... with...]: явно передать значения переменных, которые нельзя
        вывести сопоставлением с образцом

      - [specialize (H ...)]: уточнить гипотезу, указав некоторые из её
        переменных

      - [simpl]: упростить вычисления в цели

      - [simpl in H]: ... либо в гипотезе

      - [rewrite]: использовать предположение (или лемму) о равенстве, чтобы
        переписать цель

      - [rewrite ... in H]: ... или гипотезу

      - [symmetry]: заменяет цель вида [t=u] на [u=t]

      - [symmetry in H]: заменяет гипотезу вида [t=u] на [u=t]

      - [transitivity y]: доказать цель [x=z], доказав вместо этого
        [x=y] и [y=z]

      - [unfold]: заменить обозначающее на обозначаемое (раскрыть определение)
        в цели

      - [unfold... in H]: ... или в гипотезе

      - [destruct... as...]: разбор случаев по значению индуктивного типа

      - [destruct... eqn:...]: указать имя уравнения, добавляемого в контекст,
        записывающего результат разбора случаев

      - [induction... as...]: индукция по значениям индуктивного типа

      - [injection... as...]: рассуждение по инъективности на равенствах между
        значениями индуктивного типа

      - [discriminate]: рассуждение по неравенству конструкторов на равенствах
        между значениями индуктивного типа

      - [replace x with y]: заменяет [x] на [y] в текущей цели и создаёт подцель
        [x = y].

      - [assert (H: e)] (либо [assert (e) as H]): ввести "локальную лемму" [e]
        и назвать её [H]

      - [generalize dependent x]: переместить переменную [x] (и всё, что от неё
        зависит) из контекста обратно в виде явной гипотезы в цель

      - [f_equal]: заменить цель вида [f x = f y] на [x = y] *)

