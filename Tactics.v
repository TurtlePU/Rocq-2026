    Наоборот, стоит пытаться формулировать их наиболее читаемым и естественным
    способом. *)

(** Вместо этого мы можем сначала ввести все подкванторные переменные, а
    затем _заново обобщить утверждение_ по некоторым из них.  Тактика
    [generalize dependent] делает именно это. *)

Theorem double_injective_take2 : forall n m,
  double n = double m ->
  n = m.
Proof.
  intros n m.
  (* И [n], и [m] уже в контексте. *)
  generalize dependent n.
  (* Теперь [n] снова вводится в цели, так что мы можем запустить индукцию
     по [m] и получить достаточно общую индукционную гипотезу. *)
  induction m as [| m' IHm'].
  - (* m = O *) simpl. intros n eq. destruct n as [| n'] eqn:E.
    + (* n = O *) reflexivity.
    + (* n = S n' *) discriminate eq.
  - (* m = S m' *) intros n eq. destruct n as [| n'] eqn:E.
    + (* n = O *) discriminate eq.
    + (* n = S n' *) f_equal.
      apply IHm'. injection eq as goal. apply goal. Qed.

(** Давайте посмотрим на неформальное доказательство этой теоремы.
    Обратите внимание, что утверждение, которое мы доказываем по индукции,
    оставляет [n] под квантором, что соответствует нашему использованию
    [generalize dependent] в формальном доказательстве.

    _Теорема_: Для любых натуральных [n] и [m], если [double n = double m],
      то [n = m].

    _Proof_: Пусть [m] это [nat]. Докажем индукцией по [m], что для любого [n],
        если [double n = double m], то [n = m].

      - База: [m = 0], и пусть [n] -- это число такое,
        что [double n = double m].  Нам нужно доказать, что [n = 0].

        Раз [m = 0], по определению [double] мы имеем [double n = 0].  В свою
        очередь для [n] нужно рассмотреть два случая.  Если [n = 0],
        то [m = 0 = n], что и требовалось.  Иначе, если [n = S n']
        для некоторого [n'], то мы получаем противоречие: по определению
        [double] мы можем вычислить [double n = S (S (double n'))], но это
        противоречит предположению, что [double n = 0].

      - Переход: пусть [m = S m'], а [n] -- вновь такое число, что
        [double n = double m].  Мы должны показать, что [n = S m']; при этом
        предположение индукции гласит, что для любого числа [s], если
        [double s = double m'], то [s = m'].

        Благодяря тому, что [m = S m'], и определению [double], мы имеем
        [double n = S (S (double m'))].  Для [n] же есть два случая.

        Если [n = 0], то по определению [double n = 0], противоречие.

        Таким образом, мы можем предположить, что [n = S n'] для некоторого
        [n'], и вновь по определению [double] мы имеем [S (S (double n')) =
        S (S (double m'))], что по инъективности подразумевает [double n' =
        double m'].  Инстанциируя предположение индукции с [n'] позволяет нам
        заключить, что [n' = m'], из чего сразу следует, что [S n' = S m'].
        Раз [S n' = n] и [S m' = m], это ровно то, что требовалось. [] *)

