(** * Тактики [injection] и [discriminate] *)

(** Вспомним опредение натуральных чисел:

     Inductive nat : Type :=
       | O
       | S (n : nat).

    Из этого определения очевидно, что каждое число имеет следующий вид: либо
    это конструктор [O], либо оно построено применением [S] к другому числу.
    Однако это не всё, что можно сказать об этом определении; неявно в нём
    содержатся ещё два дополнительных факта:

    - Конструктор [S] _инъективен_.  То есть, если [S n = S m], то тогда
      обязательно верно [n = m].

    - Конструкторы [O] и [S] _различны_.  То есть, [O] не равно [S n]
      ни для какого [n]. *)

(** Похожие принципы применимы к любому индуктивному типу данных:
    все конструкторы инъективны, а значение, построенные с помощью различных
    конструкторов, никогда друг другу не равны. Так, у списков конструктор
    [cons] инъективность, а пустой список [nil] отличается от любого непустого
    списка.  У логических значений [true] и [false] друг другу не равны.
    И так далее. *)

(** Мы можем _доказать_ инъективность [S] с помощью функции [pred], определённой
    в [Basics.v]. *)

Theorem S_injective : forall (n m : nat),
  S n = S m ->
  n = m.
Proof.
  intros n m H1.
  assert (H2: n = pred (S n)). { reflexivity. }
  rewrite H2. rewrite H1. simpl. reflexivity.
Qed.

(** (Тактика [assert], применённая выше, добавляет указанную гипотезу
    в контекст, но сперва требует доказать гипотезу в качестве новой цели.)

    Эту технику для доказательства инъективности можно обобщить до произвольного
    конструктора, выписав для него эквивалент [pred] -- т.е. функцию,
    "отменяющую" одно применение конструктора.

    Чтобы не повторяться каждый раз, Rocq предоставляет тактику, которая
    называется [injection], которая позволяет нам использовать инъективность
    конструкторов.  Вот альтернативное доказательство теоремы выше с помощью
    [injection]: *)

Theorem S_injective' : forall (n m : nat),
  S n = S m ->
  n = m.
Proof.
  intros n m H.

(** Выписывая [injection H as Hmn], мы просим Rocq сгенерировать все уравнения,
    какие он только может, из [H], пользуясь инъективностью конструкторов
    (в текущем примере получится уравнение [n = m]). Каждое уравнение
    добавляется в качестве гипотезы (здесь она называется [Hmn]) в контекст. *)

  injection H as Hnm. apply Hnm.
Qed.

(** А вот более интересный пример, который показывает, как [injection] можно
    использовать, чтобы получить сразу несколько новых равенств. *)

Theorem injection_ex1 : forall (n m o : nat),
  [n;m] = [o;o] ->
  n = m.
Proof.
  intros n m o H.
  (* РАЗБИРАЕМ НА ЛЕКЦИИ *)
  injection H as H1 H2.
  rewrite H1. rewrite H2. reflexivity.
Qed.

