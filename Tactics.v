(** Про инъективность понятно.  Что насчёт отсутствия пересечений? *)

(** Принцип непересечения гласит, что два терма, начинающиеся с различных
    конструкторов (вроде [O] и [S], либо [true] и [false]) никогда не могут быть
    равны друг другу.  Это значит, что, как только мы оказываемся в ситуации,
    когда в контексте _предполагается_, что два таких терма равны, мы можем
    с уверенностью заявлять что угодно, поскольку исходное допущение уже ложно.
*)

(** Тактика [discriminate] воплощает этот принцип: она применяется к гипотезе,
    содержащей равенство между различными конструкторами (например,
    [false = true]), и немедленно выполняет текущую цель.  Парочка примеров: *)

Theorem discriminate_ex1 : forall (n m : nat),
  false = true ->
  n = m.
Proof.
  intros n m contra. discriminate contra. Qed.

Theorem discriminate_ex2 : forall (n : nat),
  S n = O ->
  2 + 2 = 5.
Proof.
  intros n contra. discriminate contra. Qed.

(** Эти примеры -- конкретные проявления логического принципа, известного как
    _принцип взрыва_, который утверждает, что из противоречивого утверждения
    следует что угодно (даже очевидно ложные вещи!). *)

(** Если принцип взрыва кажется вам неинтуитивным, обратите внимание, что
    эти доказательства _не_ доказывают выполнение заключения.  Вместо этого они
    говорят: _как только_ каким-то невероятным образом ложная гипотеза
    оказывается верной, _тогда_ и заключение тоже верно -- поскольку мы бы
    в такой ситуации находились во вселенной, где любое утверждение истинно.

    В следующей главе мы рассмотрим принцип взрыва более подробно. *)

(** **** Упражнение: 1 звезда, стандартное (discriminate_ex3) *)
Example discriminate_ex3 :
  forall (X : Type) (x y z : X) (l j : list X),
    x :: y :: l = [] ->
    x = z.
Proof.
  (* ЗАПОЛНИТЕ ЗДЕСЬ *) Admitted.
(** [] *)

(** В качестве более полезного примера, рассмотрим применение [discriminate]
    для установления связи между двумя различными видами равенства ([=] и [=?]),
    которые мы определяли для натуральных чисел. *)
Theorem eqb_0_l : forall n,
   0 =? n = true -> n = 0.
Proof.
  intros n.

(** Можем начать с разбора случаев по [n].
    Случай [n = 0], как всегда, тривиален. *)

  destruct n as [| n'] eqn:E.
  - (* n = 0 *)
    intros H. reflexivity.

(** Однако следующий случай выглядит не так уж просто: полагая
    [0 =? (S n') = true], мы должны показать, что [S n' = 0]!  Пробиться вперёд
    поможет наблюдение, что, вообще-то, посылка ложная: *)

  - (* n = S n' *)
    simpl.

(** Если мы используем [discriminate] на этой гипотезе, Rocq подтвердит,
    что, действительно, подцель, над которой мы работаем, недостижима, и убирает
    её из дальнейшего рассмотрения. *)

    intros H. discriminate H.
Qed.

(** Инъективность конструкторов позволяет нам установить, что
    [forall (n m : nat), S n = S m -> n = m].  Обратное утверждение -- частный
    случай более общего факта, верного как для конструкторов, так и для функций.
    Он нам очень пригодится дальше: *)

Theorem f_equal : forall (A B : Type) (f: A -> B) (x y: A),
  x = y -> f x = f y.
Proof. intros A B f x y eq. rewrite eq.  reflexivity.  Qed.

Theorem eq_implies_succ_equal : forall (n m : nat),
  n = m -> S n = S m.
Proof. intros n m H. apply f_equal. apply H. Qed.

(** Тактика `f_equal` напрямую доказывает теоремы такого вида. По цели
    вида [f a1 ... an = g b1 ... bn] она произведёт следующие подцели: [f = g],
    [a1 = b1], ..., [an = bn]. Достаточно простые подцели (например, доказуемые
    с помощью [reflexivity]) пропускаются автоматически. *)

Theorem eq_implies_succ_equal' : forall (n m : nat),
  n = m -> S n = S m.
Proof. intros n m H. f_equal. apply H. Qed.

