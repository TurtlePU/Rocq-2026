(** * Варьирование предположения индукции *)

(** Производя индуктивное доказательство в Rocq, иногда бывает важно
    проконтролировать точный вид предположения индукции. В частности, стоит быть
    осторожными по части того, какие предположения мы переносим (с помощью
    [intros]) из цели в контекст перед вызовом тактики [induction].

    Например, допустим, мы хотим показать, что [double] инъективна --
    т.е., что она отображает различные аргументы в различные результаты:

       Theorem double_injective: forall n m,
         double n = double m ->
         n = m.

    Начинаем это доказательство мы достаточно осторожно: если мы начнём его с

       intros n. induction n.

    тогда всё будет хорошо.  Однако если начать с введения _обеих_ переменных:

       intros n m. induction n.

    то мы застрянем в середине шага индукции... *)

Theorem double_injective_FAILED : forall n m,
  double n = double m ->
  n = m.
Proof.
  intros n m. induction n as [| n' IHn'].
  - (* n = O *) simpl. intros eq. destruct m as [| m'] eqn:E.
    + (* m = O *) reflexivity.
    + (* m = S m' *) discriminate eq.
  - (* n = S n' *) intros eq. destruct m as [| m'] eqn:E.
    + (* m = O *) discriminate eq.
    + (* m = S m' *) f_equal.

(** Здесь предположение индукции ([IHn']) _не_ утверждает [n' = m'] --
    мешается лишняя [S] -- так что цель нам, увы, не доказать. *)

Abort.

(** Что пошло не так? *)

(** Проблема в том, что, когда мы вызываем тактику [induction], мы уже
    ввели [m] в контекст -- другими словами, мы указали Rocq: "Рассмотрим
    некоторые [n] и [m]...", и теперь нам нужно доказать, что если
    [double n = double m] для _этих конкретных_ [n] и [m], тогда [n = m].

    Следующая тактика, [induction n], сообщает Rocq: мы докажем требуемое
    индукцией по [n].  То есть, мы собираемся доказать, что для _всех_ [n]
    выполнено следующее утверждение:

      - [P n] = "Если [double n = double m], то [n = m]"

    И показываем это мы следующим образом:

      - [P O]

         (т.е., "Если [double O = double m] то [O = m]") и

      - [P n -> P (S n)]

        (т.е., Из "Если [double n = double m], то [n = m]" следует "Если
        [double (S n) = double m], то [S n = m]").

    Если присмотреться ко второму утверждению, оно говорит нечто достаточно
    странное: так, для _конкретного_ [m], если мы знаем

      - "Если [double n = double m], то [n = m]"

    тогда мы можем доказать

       - "Если [double (S n) = double m], то [S n = m]".

    Чтобы увидеть, почему это странно, давайте подставим конкретное [m] --
    например, [5].  Утверждение тогда принимает следующий вид:

      - [Q] = "Если [double n = 10], то [n = 5]"

    И теперь нам нужно доказать

      - [R] = "Если [double (S n) = 10], то [S n = 5]".

    Но знание [Q] не поможет нам в доказательстве [R]!

    Если бы мы попытались вывести [R] из [Q], мы бы начали с чего-то вроде
    "Пусть [double (S n) = 10]...", но затем бы сразу застряли: знание того, что
    [double (S n)] это [10], не позволяет нам вывести, что [double n] это [10]
    (действительно, оно скорее сообщает, что [double n] это _не_ [10]!!),
    так что [Q] бесполезно. *)

(** Попытка доказать утверждение индукцией по [n], когда [m] уже в контексте,
    не работает, потому что так мы пытаемся доказать утверждение для
    _произвольного_ [n], но лишь для некоторого _конкретного_ [m]. *)

(** Успешное доказательство [double_injective] оставляет [m] под квантором
    вплоть до того момента, когда мы вызываем [индукцию] по [n]: *)

Theorem double_injective : forall n m,
  double n = double m ->
  n = m.
Proof.
  intros n. induction n as [| n' IHn'].
  - (* n = O *) simpl. intros m eq. destruct m as [| m'] eqn:E.
    + (* m = O *) reflexivity.
    + (* m = S m' *) discriminate eq.
  - (* n = S n' *)

(** Обратите внимание, что и цель, и предположение индукции теперь другие: цель
    просит доказать нечто более общее (т.е., нам нужно доказать утверждение
    _для любого_ [m]), но теперь и предположение индукции [IH'] более гибкое, и
    позволяет нам выбрать то [m], которое нам потребуется. *)

    intros m eq.

(** И вот только теперь мы выбираем конкретное [m] и вводим предположение, что
    [double n = double m].  Раз мы разбирали по случаям [n], также нужно
    разобрать [m], чтобы они соответствовали друг другу. *)

    destruct m as [| m'] eqn:E.
    + (* m = O *)

(** Случай с [m = 0] тривиален: *)

    discriminate eq.
    + (* m = S m' *)
      f_equal.

(** Раз уж мы находимся во второй ветви [destruct m], упомянутая в контексте
    [m'] -- это предыдущее перед [m] число.  Раз мы также находимся внутри шага
    индукции, совпадение идеальное: если мы инстанциируем [m] из предположения
    индукции в текущий [m'] (это инстанциирование будет произведено
    автоматически в ходе применения тактики [apply] в следующем шаге), то [IHn']
    окажется в точности тем, что нам нужно, чтобы закончить доказательство. *)

      apply IHn'. simpl in eq. injection eq as goal. apply goal. Qed.

