(** Проблема в том, что, для использования [m], мы должны сначала ввести [n].
    (А если мы скажем [induction m], ничего не вводя, Rocq автоматически введёт
    [n] за нас!) *)

(** Что мы можем с этим сделать?  Можно переписать утверждение леммы так,
    чтобы [m] была перечислена под квантором раньше [n].  Это сработает, но это
    не очень красиво: мы же не хотим менять формулировку лемм только для того,
