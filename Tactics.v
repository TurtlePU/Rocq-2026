(** Часть [eqn:] тактики [destruct] необязательная; хоть мы и придерживаемся
    того, чтобы её выписывать ради улучшения документируемости, её зачастую
    можно совершенно спокойно опустить.

    Один из случаев, когда её опустить _нельзя_ -- когда мы разбираем случаи
    сложного выражения; в данном случае информация, заложенная в [eqn:], может
    оказаться критически важной для доказательства и, если мы её опустим, то
    [destruct] может стереть информацию, необходимую для доказательства. *)
    (** Допустим, мы определили функцию [sillyfun1] таким образом: *)

Definition sillyfun1 (n : nat) : bool :=
  if n =? 3 then true
  else if n =? 5 then true
  else false.

(** Теперь представим, что мы хотим убедить Rocq, что [sillyfun1 n]
    может вернуть [true] только в том случае, если [n] нечётное.  Если мы начнём
    доказательство вот так (без [eqn:] в [destruct])... *)

Theorem sillyfun1_odd_FAILED : forall (n : nat),
  sillyfun1 n = true ->
  odd n = true.
Proof.
  intros n eq. unfold sillyfun1 in eq.
  destruct (n =? 3).
  (* застряли... *)
Abort.

(** ... то на этом моменте мы застрянем, потому что в контексте окажется
    недостаточно информации для доказательства цели!  Проблема в том, что
    подстановка, произведённая [destruct], достаточно жёсткая -- в данном
    случае, она выбросила все вхождения [n =? 3], однако нам нужно сохранить
    какую-то память об этом выражении и том, какой случай мы разбираем,
    поскольку далее нам нужно будет рассудить, что раз мы в этом случае полагаем
    [n =? 3 = true], то должно быть верно [n = 3], так что, действительно, [n]
    нечётное.

    Так что здесь мы хотим, действительно, заменить все вхождения [n =? 3], но
    в то же время добавить в контекст равенство, записывающее, в каком
    из случаев мы сейчас находимся.  Это в точности то, что позволяет сделать
    квалификатор [eqn:]. *)

Theorem sillyfun1_odd : forall (n : nat),
  sillyfun1 n = true ->
  odd n = true.
Proof.
  intros n eq. unfold sillyfun1 in eq.
  destruct (n =? 3) eqn:Heqe3.
  (** Здесь мы находимся в том же месте, где мы застряли выше, но в этот раз
      в контексте у нас есть дополнительная гипотеза о равенстве, и это ровно
      то, что нам нужно для продвижения. *)
    - (* e3 = true *) apply eqb_true in Heqe3.
      rewrite -> Heqe3. reflexivity.
    - (* e3 = false *)
     (** Когда мы добираемся до второй проверки на равенство в теле функции,
         о которой мы рассуждаем, мы можем вновь использовать [eqn:], чтобы
         закончить доказательство. *)
      destruct (n =? 5) eqn:Heqe5.
        + (* e5 = true *)
          apply eqb_true in Heqe5.
          rewrite -> Heqe5. reflexivity.
        + (* e5 = false *) discriminate eq.  Qed.

