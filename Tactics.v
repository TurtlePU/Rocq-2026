(** * Тактика [apply] *)

(** Мы часто оказываемся в ситуации, когда доказываемая цель _в точности_
    совпадает с некоторой гипотезой в контексте либо с какой-нибудь ранее
    доказанной леммой. *)

Theorem silly1 : forall (n m : nat),
  n = m ->
  n = m.
Proof.
  intros n m eq.

(** Здесь можно завершить с "[rewrite -> eq.  reflexivity.]", как мы делали
    раньше. Либо можно завершить в один шаг с помощью [apply]: *)

  apply eq.  Qed.

(** Тактика [apply] также работает с _обусловленными_ гипотезами
    и леммами: если применяемое утверждение это импликация, то предположения
    этой импликации будут добавлены к списку подцелей, которые требуется
    доказать.

    [apply] также работает с _обусловленными_ гипотезами: *)

Theorem silly2 : forall (n m o p : nat),
  n = m ->
  (n = m -> [n;o] = [m;p]) ->
  [n;o] = [m;p].
Proof.
  intros n m o p eq1 eq2.
  apply eq2. apply eq1.  Qed.

(** Как правило, когда мы используем [apply H], утверждение [H] начинается
    с [forall], вводящего некоторые _переменные под квантором всеобщности_.

    Когда Rocq сопоставляет текущую цель с заключением [H], он пытается найти
    подходящие значения для этих переменных.  Например, когда мы вызываем
    [apply eq2] в следующем доказательстве, переменная [q] из [eq2]
    инстанциируется в [n], а [r] заменяется на [m]. *)

Theorem silly2a : forall (n m : nat),
  (n,n) = (m,m)  ->
  (forall (q r : nat), (q,q) = (r,r) -> [q] = [r]) ->
  [n] = [m].
Proof.
  intros n m eq1 eq2.
  apply eq2. apply eq1.  Qed.

