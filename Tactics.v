(** * Тактика [apply with] *)

(** Следующий простенький пример использует два переписывания подряд, чтобы
    перейти от [[a;b]] к [[e;f]]. *)

Example trans_eq_example : forall (a b c d e f : nat),
     [a;b] = [c;d] ->
     [c;d] = [e;f] ->
     [a;b] = [e;f].
Proof.
  intros a b c d e f eq1 eq2.
  rewrite -> eq1. apply eq2. Qed.

(** Так как это частый паттерн, давайте его вынесем в качестве леммы, которая
    раз и навсегда устанавливает, что равенство транзитивно. *)

Theorem trans_eq : forall (X:Type) (x y z : X),
  x = y -> y = z -> x = z.
Proof.
  intros X x y z eq1 eq2. rewrite -> eq1. rewrite -> eq2.
  reflexivity.  Qed.

(** Теперь у нас должно получиться использовать [trans_eq], чтобы доказать
    пример выше.  Однако для этого нам потребуется небольшое изменение тактики
    [apply]. *)

Example trans_eq_example' : forall (a b c d e f : nat),
     [a;b] = [c;d] ->
     [c;d] = [e;f] ->
     [a;b] = [e;f].
Proof.
  intros a b c d e f eq1 eq2.

(** Если мы просто скажем Rocq [apply trans_eq], он (с помощью сопоставления
    цели с заключением леммы) определит, что он должен инстанциировать [X]
    в [[nat]], [x] в [[a,b]], а [z] в [[e,f]].  Однако процесс сопоставления
    не определит инстанциирование для [y]: нам придётся передать её явно,
    добавив "[with (y:=[c,d])]" к вызову [apply]. *)

  apply trans_eq with (y:=[c;d]).
  apply eq1. apply eq2.   Qed.

(** В целом, имя [y] в клозе [with] указывать не обязательно, поскольку
    Rocq зачастую достаточно умён, чтобы выяснить, какую переменную мы
    инстанциируем. Так что мы могли написать просто [apply trans_eq with [c;d]].
    *)

(** Также в Rocq есть встроенная тактика [transitivity], решающая ту же
    задачу, что и применение [trans_eq]. Ей, конечно, также нужно указать
    инстанциирование, как и в [apply with]. *)

Example trans_eq_example'' : forall (a b c d e f : nat),
     [a;b] = [c;d] ->
     [c;d] = [e;f] ->
     [a;b] = [e;f].
Proof.
  intros a b c d e f eq1 eq2.
  transitivity [c;d].
  apply eq1. apply eq2.   Qed.

