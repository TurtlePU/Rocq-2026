(** **** Упражнение: 4 звезды, стандартное (tr_rev_correct)

    Одна из проблем с определением функции [rev] состоит в том, что она вызывает
    [app] на каждом шаге.  Запуск [app] тратит время, зависящее линейно от длины
    списка, так что время работы [rev] пропорционально квадрату.

    Мы можем улучшить ситуацию с помощью следующего определения: *)

Fixpoint rev_append {X} (l1 l2 : list X) : list X :=
  match l1 with
  | [] => l2
  | x :: l1' => rev_append l1' (x :: l2)
  end.

Definition tr_rev {X} (l : list X) : list X :=
  rev_append l [].

