(** ** Конъюнкция *)

(** _Конъюнкция_ (также _логическое И_) утверждений [A] и [B] пишется как
    [A /\ B]; оно представляет утверждение, что и [A], и [B] истинны. *)

Example and_example : 3 + 4 = 7 /\ 2 * 2 = 4.

(** Чтобы доказать конъюнкцию, можно применить тактику [split].  Она сгенерирует
    две подцели, по одной на каждую часть утверждения: *)

Proof.
  split.
  - (* 3 + 4 = 7 *) reflexivity.
  - (* 2 * 2 = 4 *) reflexivity.
Qed.

(** Для любых утверждений [A] и [B], если предположить, что каждое из [A] и [B]
    верно по отдельности, мы можем заключить, что [A /\ B] также верно.
    Стандартная библиотека Rocq предоставляет функцию [conj] для этих целей. *)

Check @conj : forall A B : Prop, A -> B -> A /\ B.

(** Раз применение теоремы с гипотезами к цели генерирует по подцели
    на гипотезу, мы можем применить [conj], чтобы достигнуть того же эффекта,
    что и [split]. *)

Example and_example' : 3 + 4 = 7 /\ 2 * 2 = 4.
Proof.
  apply conj.
  - (* 3 + 4 = 7 *) reflexivity.
  - (* 2 + 2 = 4 *) reflexivity.
Qed.

(** **** Упражнение: 2 звезды, стандартное (plus_is_O) *)

Example plus_is_O :
  forall n m : nat, n + m = 0 -> n = 0 /\ m = 0.
Proof.
  (* ЗАПОЛНИТЕ ЗДЕСЬ *) Admitted.
(** [] *)

(** Вот и всё, что нужно для доказательства конъюнкции.  Чтобы пойти в обратном
    направлении -- т.е., доказать что-то ещё с помощью конъюнкции -- мы можем
    использовать старый добрый [destruct]. *)

(** когда текущий контекст доказательства содержит гипотезу [H] вида
    [A /\ B], команда [destruct H as [HA HB]] выбросит [H] из контекста
    и заменит её на две новых гипотезы: [HA] -- что [A] истинно, и [HB] --
    что [B] истинно.  *)

Lemma and_example2 :
  forall n m : nat, n = 0 /\ m = 0 -> n + m = 0.
Proof.
  (* РАЗБИРАЕМ НА ЛЕКЦИИ *)
  intros n m H.
  destruct H as [Hn Hm].
  rewrite Hn. rewrite Hm.
  reflexivity.
Qed.

(** Как обычно, мы также можем разобрать [H] сразу же в момент введения
    в контекст вместо того, чтобы вводить и разбирать по отдельности: *)

Lemma and_example2' :
  forall n m : nat, n = 0 /\ m = 0 -> n + m = 0.
Proof.
  intros n m [Hn Hm].
  rewrite Hn. rewrite Hm.
  reflexivity.
Qed.

(** Вас могло удивить, зачем это мы запаковали две гипотезы ([n = 0] и [m = 0])
    в конъюнкцию, хоть мы и могли изначально сформулировать теорему
    как требующую две дополнительных гипотезы: *)

Lemma and_example2'' :
  forall n m : nat, n = 0 -> m = 0 -> n + m = 0.
Proof.
  intros n m Hn Hm.
  rewrite Hn. rewrite Hm.
  reflexivity.
Qed.

(** Для данной конкретной теоремы обе формулировки подойдут.  Однако
    понимать как работать с гипотезами-конъюнкциями всё равно важно, поскольку
    конъюнкции часто возникают в качестве промежуточных шагов в доказательствах,
    в особенности в больших проектах.  Вот простой пример: *)

Lemma and_example3 :
  forall n m : nat, n + m = 0 -> n * m = 0.
Proof.
  (* РАЗБИРАЕМ НА ЛЕКЦИИ *)
  intros n m H.
  apply plus_is_O in H.
  destruct H as [Hn Hm].
  rewrite Hn. reflexivity.
Qed.

(** Другая частая ситуация -- мы знаем, что [A /\ B], но в текущем контексте нам
    достаточно только [A] (или только [B]).  В таких случаях мы можем вызвать
    [destruct] (возможно, неявно, как часть [intros]) и использовать
    паттерн [_], чтобы обозначить, что неиспользованный конъюнкт нужно просто
    выбросить. *)

Lemma proj1 : forall P Q : Prop,
  P /\ Q -> P.
Proof.
  intros P Q HPQ.
  destruct HPQ as [HP _].
  apply HP.  Qed.

(** **** Упражнение: 1 звезда, стандартное, по желанию (proj2) *)
Lemma proj2 : forall P Q : Prop,
  P /\ Q -> Q.
Proof.
  (* ЗАПОЛНИТЕ ЗДЕСЬ *) Admitted.
(** [] *)

(** Наконец, нам иногда может понадобиться изменить порядок конъюнкций, а также
    группировку нескольких конъюнктов. Наглядно это можно увидеть
    в следующих доказательствах коммутативности и ассоциативности: *)

Theorem and_commut : forall P Q : Prop,
  P /\ Q -> Q /\ P.
Proof.
  intros P Q [HP HQ].
  split.
    - (* left *) apply HQ.
    - (* right *) apply HP.  Qed.

(** **** Упражнение: 1 звезда, стандартное (and_assoc)

    В доказательстве ассоциативности ниже обратите внимание, как _вложенный_
    паттерн в [intros] разбивает гипотезу [H : P /\ (Q /\ R)] на [HP : P],
    [HQ : Q] и [HR : R].  Закончите доказательство. *)

Theorem and_assoc : forall P Q R : Prop,
  P /\ (Q /\ R) -> (P /\ Q) /\ R.
Proof.
  intros P Q R [HP [HQ HR]].
  (* ЗАПОЛНИТЕ ЗДЕСЬ *) Admitted.
(** [] *)

(** Инфиксная нотация [/\] -- вообще говоря, просто синтаксический сахар для
    [and A B].  Т.е. [and] -- это оператор в Rocq, принимающий два утверждения
    в качестве аргументов и возвращающий утверждение. *)

Check and : Prop -> Prop -> Prop.

