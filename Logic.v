(** Теперь перейдём к главной теореме: *)

Theorem even_bool_prop : forall n,
  even n = true <-> Even n.
Proof.
  intros n. split.
  - intros H. destruct (even_double_conv n) as [k Hk].
    rewrite Hk. rewrite H. exists k. reflexivity.
  - intros [k Hk]. rewrite Hk. apply even_double.
Qed.

(** С учётом этой теоремы, мы можем сказать, что логическое вычисление [even n]
    _отражено_ в истинности утверждения [exists k, n = double k]. *)

(** Похожим образом, утверждать равенство двух чисел [n] и [m] можно двумя
    способами:
      - (1) что [n =? m] возвращает [true], или
      - (2) что [n = m].
    И вновь эти два утверждения эквивалентны: *)

Theorem eqb_eq : forall n1 n2 : nat,
  n1 =? n2 = true <-> n1 = n2.
Proof.
  intros n1 n2. split.
  - apply eqb_true.
  - intros H. rewrite H. rewrite eqb_refl. reflexivity.
Qed.

(** И что же мы должны делать в случаях, когда некоторое утверждение можно
    формализовать и как утверждение, и как вычисление? Что нам выбрать?

    В общем случая, _оба_ представления полезны. *)

(** Например, логические значения более полезны при определении функций. Нельзя
    эффективно _проверить_, истинен ли некоторый [Prop], так что мы не можем
    использовать [Prop] в условных выражениях. Следующее определение будет
    отвергнуто: *)

Fail
Definition is_even_prime n :=
  if n = 2 then true
  else false.

(** Rocq жалуется, что [n = 2] имеет тип [Prop], а он ожидает терм типа
    [bool] (или какого-то другого индуктивного типа с двумя конструкторами).
    Это связано с _вычислительной_ природой ядра Rocq, которое устроено таким
    образом, что любая функция, которую он может выразить, обязательно вычислима
    и всюду определена. (Одна из причин состоит в том, чтобы позволить извлекать
    исполняемые программы из скриптов на Rocq.) Как следствие, [Prop] в Rocq
    _нельзя_ сопоставлять с образцом и проверять на истинность, поскольку такая
    операция позволила бы нам строить невычислимые функции.  *)

(** Вместо этого нам нужно определить эту функцию через проверку на равенство.
*)

Definition is_even_prime n :=
  if n =? 2 then true
  else false.

(** Кроме того факта, что невычислимые свойства невозможно выразить
    в качестве вычислений, даже многие _вычислимые_ свойства проще выразить
    через [Prop] а не через [bool], поскольку рекурсивные функции в Rocq
    подвержены ряду ограничений.  Так, в следующей (необязательной) главе будет
    показано, как определить с помощью [Prop] свойство, что регулярное выражение
    допускает полученную строку.  Аналогичное построение с [bool] свелось бы к
    написанию программы сопоставления регулярного выражения со строкой, что было
    бы более сложно для написания, понимания и доказывания свойств, чем простое
    (не-алгоритмическое) определение этого свойства.

    С другой стороны, важным продуктом выписывания утверждений с помощью
    логических значений является автоматизация доказательств с помощью
    вычислений на термах; эта техника известна под названием
    _доказательство рефлексией_.

    Рассмотрим следующее утверждение: *)

Example even_1000 : Even 1000.

(** Самый прямолинейный способ доказательства -- явно указать значение [k]. *)

Proof. unfold Even. exists 500. reflexivity. Qed.

(** Доказательство соответствующего утверждения о логических значениях проще,
    поскольку нам не нужно искать значение свидетеля: механизм вычислений Rocq
    делает это за нас! *)

Example even_1000' : even 1000 = true.
Proof. reflexivity. Qed.

(** Теперь, полезно заметить, что раз эти два понятия эквивалентны, мы можем
    использовать формулировку через логические значения, чтобы доказать
    утверждение, не упоминая 500 явно: *)

Example even_1000'' : Even 1000.
Proof. apply even_bool_prop. reflexivity. Qed.

(** Хоть мы и не особо выиграли по числу строчек, доказательства большего
    размера можно существенно упростить с помощью рефлексии.  Так, знаменитое
    доказательство _теоремы о четырёх красках_ на Rocq использует рефлексию,
    чтобы свести анализ сотен различных случаев к логическим вычислениям. *)

(** Другое достоинство логических значений в том, что _отрицание_ утверждений
    о логических значениях проще как формулировать, так и доказывать: просто
    проотрицаем ожидаемый результат вычислений. *)

