(** **** Упражнение: 3 звезды, сложное (not_exists_dist)

    В классической логике выводимо, что следующие два утверждения эквивалентны:

    ~ (exists x, ~ P x)
    forall x, P x

    Теорема [dist_not_exists] выше доказывает одну из частей этой
    эквивалентности. Оказывается, обратное направление в конструктивной логике
    не доказать. Ваша работа состоит в том, чтобы показать, что оно следует из
    исключённого третьего. *)

