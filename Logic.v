(** Неравенство -- очень частая форма отрицательного утверждения, так что у него
    есть специальная нотация: *)

Notation "x <> y" := (~(x = y)) : type_scope.

(** Например: *)

Theorem zero_not_one : 0 <> 1.
Proof.
  (** Утверждение [0 <> 1] в точности совпадает с [~(0 = 1)] -- то есть,
      [not (0 = 1)] -- что раскрывается в [(0 = 1) -> False]. (Мы используем
      [unfold not] явно, чтобы проиллюстрировать это замечание, но, как правило,
      этот шаг можно пропустить.) *)
  unfold not.
  (** Чтобы доказать неравенство, можно предположить равенство... *)
  intros contra.
  (** ... и вывести противоречие. В данном случае, равенство [O = S O]
      противоречит различности конструкторов [O] и [S], так что [discriminate]
      позаботится об этом. *)
  discriminate contra.
Qed.

(** Чтобы привыкнуть к работе с отрицанием в Rocq, надо немного
    попрактиковаться. Даже если _Вам_ прекрасно видно, почему утверждение,
    содержащее отрицание, выполнено, может понадобиться пара трюков, чтобы
    заставить Rocq это тоже понять!

    Вот доказательства нескольких понятных фактов для разогрева. *)

Theorem not_False :
  ~ False.
Proof.
  unfold not. intros H. destruct H. Qed.

Theorem contradiction_implies_anything : forall P Q : Prop,
  (P /\ ~P) -> Q.
Proof.
  (* РАЗБИРАЕМ НА ЛЕКЦИИ *)
  intros P Q [HP HNP]. unfold not in HNP.
  apply HNP in HP. destruct HP.  Qed.

Theorem double_neg : forall P : Prop,
  P -> ~~P.
Proof.
  (* РАЗБИРАЕМ НА ЛЕКЦИИ *)
  intros P H. unfold not. intros G. apply G. apply H.  Qed.

