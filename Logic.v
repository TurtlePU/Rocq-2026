Proof.
  reflexivity.
Qed.

(** В свою очередь, с отрицаниями утверждений работать напрямую сложнее.

    Так, пусть мы сформулировали утверждение о нечётности [1001]: *)

Example not_even_1001' : ~(Even 1001).

(** Доказывать его напрямую -- предполагая, что есть некоторое [n] такое, что
    [1001 = double n], а затем как-то придти к противоречию -- будет достаточно
    сложно.

    Но если мы сконвертируем его к рассуждению о функции [even], мы можем
