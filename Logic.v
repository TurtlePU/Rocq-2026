    рекурсивный вызов функции является последней операцией, которую нужно
    выполнить (т.е. нам не нужно выполнять [++] после рекурсивного вызова);
    хороший компилятор для этого случая сгенерирует крайне эффективный код.

    Докажите, что [rev] и [tr_rev] действительно эквивалентны. *)

