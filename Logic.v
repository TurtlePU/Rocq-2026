Set Warnings "-notation-overridden".
Require Nat.
From Lectures Require Export Tactics.

(** Мы увидели много примеров _утверждений_, а также способов их
    освидетельствовать (_доказательств_).  В частности, мы много работали
    с равенствами ([e1 = e2]), следствиями ([P -> Q]) и утверждениями
    с квантором всеобщности ([forall x, P]).  В этой главе мы увидим, как Rocq
    можно использовать для выполнения других знакомых Вам способов логических
    рассуждений.

    Перед тем, как погрузиться в детали, стоит немного поговорить о положении
    математических утверждений в Rocq. Rocq -- _типизированный_ язык, так что
    каждое корректное выражение в нём имеет соответствующий тип. Логические
    утверждения не являются исключением: каждое утверждение, которое мы можем
    попытаться доказать в Rocq, имеет тип [Prop], собственно, тип _утверждений_.
    Мы можем убедиться в этом с помощью команды [Check]: *)

Check (forall n m : nat, n + m = m + n) : Prop.

(** Обратите внимание, что _все_ синтаксически корректные утверждения имеют тип
    [Prop], вне зависимости от того, верны они или нет.

    Просто _быть_ утверждением это одно дело; быть _доказуемым_ -- уже совсем
    другая история! *)

Check 2 = 2 : Prop.

Check 3 = 2 : Prop.

Check forall n : nat, n = 2 : Prop.

(** На самом деле утверждения не просто имеют тип -- это _объекты первого
    класса_, которыми можно распоряжаться так же, как и со всеми остальными
    вещами в Rocq. *)

(** Ранее, мы видели утверждения в единственном месте: в объявлениях [Теорем]
    (а также [Лемм] и [Примеров]). *)

Theorem plus_2_2_is_4 :
  2 + 2 = 4.
Proof. reflexivity.  Qed.

(** Но утверждениями можно пользоваться и по-другому.  Например, именовать
    утверждения можно с помощью [Определений], точно так же, как мы именуем
    другие выражения. *)

Definition plus_claim : Prop := 2 + 2 = 4.
Check plus_claim : Prop.

(** Позднее мы сможем использовать это имя в любом месте, где ожидается
    утверждение -- например, в качестве формулировки [Теоремы]. *)

Theorem plus_claim_is_true :
  plus_claim.
Proof. reflexivity.  Qed.

(** Также мы можем формулировать _параметризованные_ утверждения -- другими
    словами, функции, принимающие аргументы некоторого типа и возвращающие
    утверждение. *)

(** Например, следующая функция принимает число и возвращает утверждение,
    заявляющее, что это число равно трём: *)

Definition is_three (n : nat) : Prop :=
  n = 3.
Check is_three : nat -> Prop.

(** Функции, возвращающие утверждения, также называют _свойствами_ своих
    аргументов.

    Вот, например, (полиморфное) свойство, формализующее знакомое понятие
    _инъективной функции_. *)

Definition injective {A B} (f : A -> B) : Prop :=
  forall x y : A, f x = f y -> x = y.

Lemma succ_inj : injective S.
Proof.
  intros x y H. injection H as H1. apply H1.
Qed.

(** Уже знакомый нам оператор [=] -- это (бинарная) функция,
    возвращающая [Prop].

    Выражение [n = m] -- это [Нотация] для [eq n m], определённая в стандартной
    библиотеке Rocq.

    Поскольку [eq] можно применять к аргументам любого типа, она также
    полиморфна: *)

Check @eq : forall A : Type, A -> A -> Prop.

(** (Обратите внимание, что мы написали [@eq] вместо [eq]: аргумент-тип
    [A] в [eq] объявлен как неявный.) *)

