    специализированную версию [add_comm], пригодную для того переписывания,
    которое нам нужно. *)

Lemma add_comm3_take2 :
  forall x y z, x + (y + z) = (z + y) + x.
Proof.
  intros x y z.
  rewrite add_comm.
  assert (H : y + z = z + y).
    { rewrite add_comm. reflexivity. }
  rewrite H.
  reflexivity.
Qed.

(** Более элегантным подходом было бы применить [add_comm] сразу к тем
    аргументам, которые нужны для инстанциирования, таким же образом, как мы
    применяем полиморфную функцию к аргументу-типу. *)

Lemma add_comm3_take3 :
  forall x y z, x + (y + z) = (z + y) + x.
Proof.
  intros x y z.
  rewrite add_comm.
  rewrite (add_comm y z).
  reflexivity.
Qed.

(** Если бы мы этого действительно хотели, мы могли бы проделать это для обоих
    переписываний. *)

Lemma add_comm3_take4 :
  forall x y z, x + (y + z) = (z + y) + x.
Proof.
  intros x y z.
  rewrite (add_comm x (y + z)).
  rewrite (add_comm y z).
  reflexivity.
Qed.

(** И ещё один пример, теперь с использованием теоремы о списках в качестве
    функции.  Пусть мы доказали следующий простой факт о списках... *)

Theorem in_not_nil :
  forall A (x : A) (l : list A), In x l -> l <> [].
Proof.
  intros A x l H. unfold not. intro Hl.
  rewrite Hl in H.
  simpl in H.
  apply H.
Qed.

(** (Другими словами, если список [l] содержит элемент [x], то [l]
    не может быть пустым.) *)

(** Обратите внимание, что подкванторная переменная [x] не появляется
    в заключении ([l <> []]). *)

(** Интуитивно, мы должны бы суметь использовать эту теорему для доказательства
    частного случая, в котором [x] это [42]. Однако обычное применение тактики
    [apply in_not_nil] провалится, поскольку оно не может вывести значение [x].
*)

Lemma in_not_nil_42 :
  forall l : list nat, In 42 l -> l <> [].
Proof.
  intros l H.
  Fail apply in_not_nil.
Abort.

(** Для обхода этой проблемы есть несколько способов: *)

(** Можем использовать [apply ... with ...]: *)
Lemma in_not_nil_42_take2 :
  forall l : list nat, In 42 l -> l <> [].
Proof.
  intros l H.
  apply in_not_nil with (x := 42).
  apply H.
Qed.

(** Или можно использовать [apply ... in ...]: *)
Lemma in_not_nil_42_take3 :
  forall l : list nat, In 42 l -> l <> [].
Proof.
  intros l H.
  apply in_not_nil in H.
  apply H.
Qed.

(** Или -- вот новый способ -- можем явно применить лемму с [42] вместо [x]: *)
Lemma in_not_nil_42_take4 :
  forall l : list nat, In 42 l -> l <> [].
Proof.
  intros l H.
  apply (in_not_nil nat 42).
  apply H.
Qed.

(** Также мы можем явно применить лемму к гипотезе, так что значения остальных
    параметров выведутся сами: *)
Lemma in_not_nil_42_take5 :
  forall l : list nat, In 42 l -> l <> [].
Proof.
  intros l H.
  apply (in_not_nil _ _ _ H).
Qed.

(** Вы можете "использовать теорему как функцию" таким образом с практически
    любой тактикой, принимающей имя теоремы в качестве аргумента.

    Также обратите внимание, что применение теоремы использует те же механизмы
    вывода типов, что и применение функции; так, можно, например, использовать
    вайлдкарды вместо аргументов, или объявлять некоторые аргументы теоремы
    неявными по умолчанию.

    Эти техники проиллюстрированы в доказательстве ниже. (Детали того, как это
    доказательство работает, не очень важны -- цель здесь проиллюстрировать
    технику применения теорем к аргументам.) *)

Example lemma_application_ex :
  forall {n : nat} {ns : list nat},
    In n (map (fun m => m * 0) ns) ->
    n = 0.
Proof.
  intros n ns H.
  destruct (proj1 _ _ (In_map_iff _ _ _ _ _) H)
           as [m [Hm _]].
  rewrite mul_0_r in Hm. rewrite <- Hm. reflexivity.
Qed.

