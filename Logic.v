(** ** Функциональная экстенсиональность *)

(** Логика Rocq достаточно минималистична.  Это значит, что иногда перевод
    рассуждений со "стандартно-математического" бывает очень многословен -- или
    даже невозможен -- без обогащения базовой логики Rocq дополнительными
    аксиомами. *)

(** Например, утверждения о равенствах, которые мы видели до сих пор,
    формулировались относительно значений индуктивных типов ([nat], [bool]
    и т.д.).  Но раз оператор равенства в Rocq полиморфен, мы можем использовать
    его для _любых_ типов -- в частности, мы можем выписывать утверждения, что
    две _функции_ равны друг другу:

    В некоторых случаях Rocq успешно доказывает равенство двух _функций_: **)

Example function_equality_ex1 :
  (fun x => 3 + x) = (fun x => (pred 4) + x).
Proof. reflexivity. Qed.

(** Это случается тогда, когда Rocq может упростить функции до одинакового
    выражения, но это происходит не всегда. **)

(** Функции из примера выше равны по упрощению, но в общем случае функции могут
    равняться друг другу по более интересным причинам.

    В общематематической практике, две функции [f] и [g] считаются равными, если
    они производят одинаковый результат на одинаковых входах:

    (forall x, f x = g x) -> f = g

    Это также известно как принцип _функциональной экстенсиональности_. *)

(** (Неформально говоря, "экстенсиональное" свойство -- это то, которое присуще
    наблюдаемому поведению объекта.  Так, функциональная экстенсиональность по
    сути утверждает, что функция полностью определяется своим наблюдаемым
    поведением -- т.е. тем, какие результаты мы получаем от её применения.) *)

(** Однако функциональная экстенсиональность не является частью встроенной
    логики Rocq. Это значит, что некоторые интуитивно очевидные утверждения
    недоказуемы. *)

Example function_equality_ex2 :
  (fun x => plus x 1) = (fun x => plus 1 x).
Proof.
  Fail reflexivity. Fail rewrite add_comm.
  (* Застряли *)
Abort.

(** Однако, если мы хотим, мы можем добавить функциональную экстенсиональность
    в Rocq с помощью команды [Axiom]. *)

Axiom functional_extensionality : forall {X Y: Type}
                                    {f g : X -> Y},
  (forall (x:X), f x = g x) -> f = g.

(** Определять что-то как [Аксиому] имеет тот же эффект, как сформулировать
    теорему и пропустить её доказательство с помощью [Admitted], но оно
    оповещает читателя, что это не просто незаконченное доказательство,
    к которому потом можно вернуться! *)

(** Теперь мы можем применять функциональную экстенсиональность
    в доказательствах: *)

Example function_equality_ex2 :
  (fun x => plus x 1) = (fun x => plus 1 x).
Proof.
  apply functional_extensionality. intros x.
  apply add_comm.
Qed.

(** Естественно, нужно быть достаточно аккуратными при добавлении новых аксиом
    в логику Rocq, ведь так можно сделать её _противоречивой_ -- то есть, в ней
    станет возможным доказательство любого утверждения, включая [False], [2+2=5]
    и т.д.!

    В общем случае, простого способа определить безопасность аксиомы нет:
    для того, чтобы убедиться в здравости некоторого конкретного набора аксиом,
    зачастую требуется тяжёлая работа высококвалифицированных математиков.

    К счастью, нам известно, что добавление функциональной экстенсиональности,
    действительно, непротиворечиво. *)

