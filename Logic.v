(** * Работа с разрешимыми утверждениями *)

(** Мы видели два разных способа выражения логических утверждений в Rocq:
    _логическими значениями_ (типа [bool]), а также _утверждениями_
    (типа [Prop]).

    Ключевые отличия между [bool] и [Prop]:

                               bool     Prop
                               ====     ====
           разрешимо?          да        нет
           match?              да        нет
           переписывания?      нет       да
*)

(** Критическое различие между этими двумя мирами -- _разрешимость_.
    Каждое выражение типа [bool] в пустом контексте можно за конечное число
    шагов упростить либо до [true], либо до [false] -- т.е., существует
    завершающаяся автоматическая процедура, решающая, является ли значение
    [true].

    Это значит, что, например, тип [nat -> bool] содержит только такие термы,
    которые по [nat] всегда возвращают либо [true], либо [false], причём
    за конечное время; в свою очередь, это значит (по диагональному аргументу)
    что в Rocq _нельзя_ определить функцию типа [nat -> bool], проверяющую,
    является ли данное число кодом завершающейся машины Тьюринга.

    В свою очередь, тип [Prop] включает как разрешимые, так и неразрешимые
    математические утверждения; в частности, тип [nat -> Prop] содержит функции,
    представляющие свойства вроде "n-я машина Тьюринга завершается".

    Второй ряд в таблице напрямую получается из этого ключевого отличия.  Чтобы
    вычислить сопоставление с образцом (либо условный оператор), нам нужно
    знать, вычисляется ли логическое значение в [true] или [false]; это работает
    только для [bool], не для [Prop].

    Третья строка подсвечивает важное практическое отличие: функцию проверки
    равенства вроде [eqb_nat], возвращающую логическое значение, нельзя
    использовать напрямую для переписывания; для этого нужно
    равенство-утверждение. *)


(** Раз [Prop] включает как разрешимые, так и неразрешимые свойства, у нас
    появляется два варианта, когда мы хотим формализовать разрешимое свойство:
    можно выразить его и как вычисление на логических значениях, и как функцию
    в [Prop]. *)

Example even_42_bool : even 42 = true.
Proof. reflexivity. Qed.

(** ... так и что существует [k] такое, что [n = double k]. *)
Example even_42_prop : Even 42.
Proof. unfold Even. exists 21. reflexivity. Qed.

(** Конечно, было бы очень странно, если бы эти две характеристики чётности не
    описывали одно и то же множество натуральных чисел.

    К счастью, описывают! *)

(** Чтобы доказать это, нам нужны две вспомогательные леммы. *)

Lemma even_double : forall k, even (double k) = true.
Proof.
  intros k. induction k as [|k' IHk'].
  - reflexivity.
  - simpl. apply IHk'.
Qed.

(** **** Упражнение: 3 звезды, стандартное (even_double_conv) *)
