(** ** Классическая и конструктивная логики *)

(** Мы убедились, что при определении функции в Rocq проверить истинность
    произвольного утверждения [P] нельзя.  Возможно, вы будете удивлены, когда
    узнаете, что похожее ограничение применяется к _доказательствам_! Другими
    словами, следующий интуитивный принцип рассуждений в Rocq не выводится: *)

Definition excluded_middle := forall P : Prop,
  P \/ ~ P.

(** Чтобы "вычислительно" понять, почему это так, вспомним, что, чтобы
    доказать утверждение вида [P \/ Q], мы используем тактики [left] и [right],
    которые по сути требуют знания о том, какая из частей дизъюнкции выполнена.
    Однако [P] под квантором всеобщности в [excluded_middle] -- _произвольное_
    утверждение, о котором мы ничего не знаем.  У нас недостаточно информации,
    чтобы выбрать, какую из тактик применить, [left] или [right]. *)

(** Однако, в особом случае, когда мы значем, что [P] отражено в некотором
    логическом значении [b], выяснение истинности [P] тривиально: просто
    проверим значение [b]. *)

Theorem restricted_excluded_middle : forall P b,
  (P <-> b = true) -> P \/ ~ P.
Proof.
  intros P [] H.
  - left. rewrite H. reflexivity.
  - right. rewrite H. intros contra. discriminate contra.
Qed.

(** В частности, исключённое третье верно для равенств [n = m] между
    натуральными числами [n] и [m]. *)

Theorem restricted_excluded_middle_eq : forall (n m : nat),
  n = m \/ n <> m.
Proof.
  intros n m.
  apply (restricted_excluded_middle (n = m) (n =? m)).
  symmetry.
  apply eqb_eq.
Qed.

(** К сожалению, этот трюк работает только для разрешимых утверждений. *)

(** Может показаться странным, что исключённое третье не доступно
    по умолчанию в Rocq, хоть это и совершенно стандартная аксиома более
    стандартных логик вроде ZFC.  Но в _не_ принятии исключённого третьего есть
    явное преимущество: утверждения в Rocq утверждают больше, чем аналогичные
    утверждения в стандартной математике.  Так, доказательство [exists x, P x]
    в Rocq всегда содержит в себе конкретное значение [x], для которого может
    быть доказано [P x] -- другими словами, каждое доказательство существования
    _конструктивно_. *)

(** Логики вроде используемой в Rocq's, не предполагающие исключённое третье
    по умолчанию, называют _конструктивными логиками_.

    Логические системы вроде ZFC, в которых исключённое третье верно для
    произвольных утверждений, называют _классическими_. *)

(** Следующий пример иллюстрирует, почему принятие исключённого третьего может
    приводить к неконструктивным доказательствам:

    _Утверждение_: Существуют такие рациональные [a] и [b], что [a ^ b]
    ([a] в степени [b]) рационально.

    _Доказательство_: Легко заметить, что [sqrt 2] иррационально.  Так что, если
    [sqrt 2 ^ sqrt 2] рационально, достаточно взять [a = b = sqrt 2].
    В противном случае, [sqrt 2 ^ sqrt 2] иррационально.  Тогда возьмём
    [a = sqrt 2 ^ sqrt 2] и [b = sqrt 2], ведь
    [a ^ b = sqrt 2 ^ (sqrt 2 * sqrt 2) = sqrt 2 ^ 2 = 2].  []

    Видите, что здесь произошло?  Мы использовали исключённое третье, чтобы
    рассмотреть случаи относительно того, является ли [sqrt 2 ^ sqrt 2]
    рациональным или нет, хотя мы не знаем, что из этого верно! Из-за этого, мы
    завершаем доказательство, зная, что такие [a] и [b] существуют, но не зная
    их конкретных значений.

    Насколько конструктивная логика полезна, настолько же она имеет свои
    ограничения: множество утверждений, легко доказуемых в классической логике,
    имеют гораздо более сложные конструктивные доказательства, а для некоторых
    из них конструктивных доказательств и вовсе нет! К счастью, как и
    функциональная экстенсиональность, исключённое третье совместимо с логикой
    Rocq, и мы можем добавить её как аксиому. Правда, мы не будем этого здесь
    делать: те результаты, которые мы получим в рамках Оснований ПО, полностью
    формализуются в конструктивной логике, с совсем небольшими дополнительными
    сложностями.

    Некоторая практика нужна, чтобы понять, каких способов рассуждений лучше
    избегать в конструктивной математике, но чаще всего причиной являются
    доказательства от противного. Вот типичный пример: пусть мы хотим показать,
    что существует некоторый [x], обладающий некоторым свойством [P], т.е. что
    [P x] верно.  Мы начинаем с того, что предполагаем, что наше утверждение
    ложно; то есть, [~ exists x, P x]. Из этого допущения легко вывести, что
    [forall x, ~ P x].  Если мы покажем, что из этого следует противоречие, то
    мы получим доказательство существования, так и не предъявив [x] такой, что
    [P x] выполнено!

    С конструктивной точки зрения проблема в том, что мы утверждаем,
    что доказали [exists x, P x], используя доказательство
    [~ ~ (exists x, P x)]. Разрешать себе удалять двойные отрицания произвольных
    утверждений эквивалентно принятию закона исключённого третьего, как показано
    в одном из упражнений ниже.  Так, такой способ рассуждений нельзя погрузить
    в Rocq без принятия дополнительных аксиом. *)

(** **** Упражнение: 3 звезды, стандартное (excluded_middle_irrefutable)

    Доказывать непротиворечивость Rocq с принятой аксиомой исключённого третьего
    требует сложных рассуждений, которые нельзя произвести внутри самого Rocq.
    Однако следующая теорема подразумевает, что принимать аксиому разрешимости
    (т.е. конкретный случай исключённого третьего) для любого _конкретного_
    утверждения [P] безопасно.  Почему?  Потому что отрицание этой аксиомы ведёт
    к противоречию.  Если бы [~ (P \/ ~P)] было бы доказуемо, тогда,
    по доказанному выше [de_morgan_not_or], было бы доказуемо [P /\ ~P], что
    привело бы к противоречию. Так что добавлять [P \/ ~P] для каждого
    конкретного [P] безопасно.

    Кратко: для любого утверждения P,
        [Rocq непротиворечив ==> Rocq + (P \/ ~P) непротиворечив]. *)

Theorem excluded_middle_irrefutable: forall (P : Prop),
  ~ ~ (P \/ ~ P).
Proof.
  (* ЗАПОЛНИТЕ ЗДЕСЬ *) Admitted.
