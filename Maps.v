(** * Maps: Тотальные и частичные словари *)

(** _Мапы_ (или _словари_) постоянно встречаются как в обычном программировании,
    так и в теории языков программирования; в последующих главах они нам очень
    пригодятся.

    Одновременно с этим, на них будет удобно отработать техники, представленные
    в предыдущих главах, включая построение структур данных с помощью функций
    высшего порядка (как в [Basics] и [Poly]) и использование рефлексии
    для упрощения доказательств (главы [Logic], [IndProp]).

    Мы определим две разновидности словарей: _тотальные_ включают элемент
    "по умолчанию", возвращаемый, если ключа нет в словаре; _частичные_
    возвращают [option], чтобы иметь возможность сообщать об ошибке.  Частичные
    словари определяются как тотальные, где в качестве элемента по умолчанию
    используется [None]. *)

