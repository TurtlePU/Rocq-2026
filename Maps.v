(** * Тотальные словари *)

(** Нашей главной задачей в этой главе будет построить определение частичных
    отображений, которое будет похоже по поведению на словари из главы
    [Lists], плюс доказать леммы об их поведении.

    Правда, в этот раз для построения словарей мы будем использовать _функции_
    вместо списка пар ключ-значение.  Преимущество такого представления в том,
    что оно предлагает более "экстенсиональный" взгляд на словари: два словаря,
    которые отвечают на запросы одинаково, будут представлены одной и той же
    функцией, а не просто "эквивалентными" списками.  Это упростит
    доказательства, использующие словари. *)

(** Мы строим частичные словари в два этапа.  На первом мы определяем тип
    _тотальных словарей_, которые возвращают значение по умолчанию, когда мы
    запрашиваем ключ, отсутствующий в словаре. *)

Definition total_map (A : Type) := string -> A.

(** По сути, тотальный словарь элементов типа [A] -- это просто функция, которая
    по [string]ам возвращает элементы типа [A]. *)

(** Функция [t_empty] по элементу-по-умолчанию возвращает пустой тотальный
    словарь; этот словарь для любого ключа возвращает этот самый элемент
    по умолчанию. *)

Definition t_empty {A : Type} (v : A) : total_map A :=
  (fun _ => v).

(** Функция обновления словаря будет поинтереснее. Как и ожидается, она получает
    словарь [m], ключ [x] и значение [v] и возвращает новый словарь, который
    ассоциирует [x] с [v], а остальные ключи ассоциированы так же, как в [m].
    Новизна здесь в том, что мы достигаем этого эффекта, как бы оборачивая новую
    функцию вокруг старой. *)

Definition t_update {A : Type} (m : total_map A)
                    (x : string) (v : A) :=
  fun x' => if String.eqb x x' then v else m x'.

(** Это определение -- хороший пример программирования функций высшего порядка:
    [t_update] получает _функцию_ [m] и возвращает новую функцию
    [fun x' => ...], которая ведёт себя как нам хочется. *)

(** Например, мы можем построить словарь, связывающий [string]и с [bool]ями, где
    ["foo"] и ["bar"] отображаются в [true], а все остальные ключи -- в [false],
    вот так: *)

Definition examplemap :=
  t_update (t_update (t_empty false) "foo" true)
           "bar" true.

(** Далее, давайте введём несколько нотаций для работы со словарями. *)

(** Во-первых, мы используем следующую нотацию для представления пустого
    тотального словаря со значением по умолчанию. *)
Notation "'__' '!->' v" := (t_empty v)
  (at level 100, right associativity).

Example example_empty := (__ !-> false).

(** Далее мы вводим нотацию для расширения существующего словаря новым
    присваиванием. *)
Notation "x '!->' v ';' m" := (t_update m x v)
                                (at level 100, v constr at level 100, right associativity).

(** Теперь [examplemap] можно определить вот так: *)

Definition examplemap' :=
  ( "bar" !-> true;
    "foo" !-> true;
    __    !-> false
  ).

(** На этом определение тотальных словарей заканчивается.
    Обратите внимание, что нам не нужно определять опреацию [find] на этом
    представлении, поскольку это будет просто применение функции! *)

Example update_example1 : examplemap' "baz" = false.
Proof. reflexivity. Qed.

Example update_example2 : examplemap' "foo" = true.
Proof. reflexivity. Qed.

Example update_example3 : examplemap' "quux" = false.
Proof. reflexivity. Qed.

Example update_example4 : examplemap' "bar" = true.
Proof. reflexivity. Qed.

(** Когда мы будем использовать словари в последующих главах, нам потребуется
    несколько фундаментальных фактов об их поведении. *)

(** Даже если Вы не проделаете следующие упражнения, убедитесь, что Вы как
    следует понимаете утверждения лемм! *)

(** (Некоторые доказательства требуют экстенсиональности функций, которую
    мы обсудили в главе [Logic].) *)

(** **** Упражнение: 1 звезда, стандартное, по желанию (t_apply_empty)

    Во-первых, пустой словарь всегда возвращает элемент по умолчанию: *)

Lemma t_apply_empty : forall (A : Type) (x : string) (v : A),
  (__ !-> v) x = v.
Proof.
  (* ЗАПОЛНИТЕ ЗДЕСЬ *) Admitted.
(** [] *)

(** **** Упражнение: 2 звезды, стандартное, по желанию (t_update_eq)

    Далее, если мы обновим отображение [m] в ключе [x] новым значением [v], а
    затем запросим значение по ключу [x] в новом словаре, мы получим [v]: *)

Lemma t_update_eq : forall (A : Type) (m : total_map A) x v,
  (x !-> v ; m) x = v.
Proof.
  (* ЗАПОЛНИТЕ ЗДЕСЬ *) Admitted.
(** [] *)

(** **** Упражнение: 2 звезды, стандартное, по желанию (t_update_neq)

    С другой стороны, если мы обновим словарь [m] в ключе [x1], а затем запросим
    значение по _другому_ ключу [x2] в новом словаре, мы получим тот же
    результат, который бы дал словарь [m]: *)

Theorem t_update_neq : forall (A : Type) (m : total_map A) x1 x2 v,
  x1 <> x2 ->
  (x1 !-> v ; m) x2 = m x2.
Proof.
  (* ЗАПОЛНИТЕ ЗДЕСЬ *) Admitted.
(** [] *)

(** **** Упражнение: 2 звезды, стандартное, по желанию (t_update_shadow)

    Если мы обновим словарь [m] в ключе [x] значением [v1], а затем -- снова
    обновим, по тому же ключу [x], но уже другим значением [v2] -- то
    получившийся словарь будет вести себя так же (возвращать те же результаты,
    будучи применённым к тем же ключам), как и словарь, который получается
    только вторым обновлением [m]: *)

Lemma t_update_shadow : forall (A : Type) (m : total_map A) x v1 v2,
  (x !-> v2 ; x !-> v1 ; m) = (x !-> v2 ; m).
Proof.
  (* ЗАПОЛНИТЕ ЗДЕСЬ *) Admitted.
(** [] *)

(** **** Упражнение: 2 звезды, стандартное (t_update_same)

    Получив строчки [x1] и [x2], мы можем использовать тактику
    [destruct (eqb_spec x1 x2)], чтобы одновременно произвести разбор случаев
    по результату [String.eqb x1 x2] и сгенерировать гипотезы о равенстве ([=])
    [x1] и [x2].  Используя пример из главы [IndProp] в качестве шаблона,
    используйте [String.eqb_spec] для доказательства следующей теоремы, которая
    утверждает, что если мы обновим словарь [m], присвоив ключу [x] то же
    значение, которое ему дано в [m], то результат будет равен [m]: *)

Theorem t_update_same : forall (A : Type) (m : total_map A) x,
  (x !-> m x ; m) = m.
Proof.
  (* ЗАПОЛНИТЕ ЗДЕСЬ *) Admitted.
(** [] *)

(** **** Упражнение: 3 звезды, стандартное, очень полезное (t_update_permute)

    Похожим образом, используйте [String.eqb_spec], чтобы доказать ещё одно
    свойство функции [update]: если мы обновим словарь [m] двумя различными
    ключами, неважно, в каком порядке мы производим обновления. *)

Theorem t_update_permute : forall (A : Type) (m : total_map A)
                                  v1 v2 x1 x2,
  x2 <> x1 ->
  (x1 !-> v1 ; x2 !-> v2 ; m)
  =
  (x2 !-> v2 ; x1 !-> v1 ; m).
Proof.
  (* ЗАПОЛНИТЕ ЗДЕСЬ *) Admitted.
(** [] *)

