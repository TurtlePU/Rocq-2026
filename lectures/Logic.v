(** ** Ложь и отрицание *)

(** До сего момента, мы по большей части занимались доказательством
    "положительных" утверждений -- сложение коммутативно, конкатенация
    ассоциативна и т.д.  Однако иногда мы также заинтересованы в доказательстве
    отрицательных результатов, демонстрирующих, что некоторое утверждение
    _не_верно. Такие утверждения выражаются с помощью оператора логического
    отрицания [~]. *)

(** Чтобы осознать, как работает отрицание, вспомним _принцип взрыва_ из главы
    [Tactics], утверждающий, что, если мы допускаем противоречие, то можно
    доказать любое другое утверждение.

    Следуя этой интуиции, мы могли бы определить [~ P] ("не [P]") как
    [forall Q, P -> Q]. *)

(** Rocq, в целом, делает эквивалентный выбор, определяя [~ P] как [P -> False],
    где [False] -- не-доказуемое утверждение из стандартной библиотеки. *)

Module NotPlayground.

Definition not (P:Prop) := P -> False.

Check not : Prop -> Prop.

Notation "~ x" := (not x) : type_scope.

End NotPlayground.

(** Раз [False] -- противоречивое утверждение, к нему также применим принцип
    взрыва. Если в контексте оказывается [False], мы можем применить к нему
    [destruct], чтобы выполнить любую цель: *)

Theorem ex_falso_quodlibet : forall (P:Prop),
  False -> P.
Proof.
  intros P contra.
  destruct contra.  Qed.

(** Неравенство -- очень частая форма отрицательного утверждения, так что у него
    есть специальная нотация: *)

Notation "x <> y" := (~(x = y)) : type_scope.

(** Например: *)

Theorem zero_not_one : 0 <> 1.
Proof.
    unfold not.
    intros contra.
    discriminate contra.
Qed.

(** Чтобы привыкнуть к работе с отрицанием в Rocq, надо немного
    попрактиковаться. Даже если _Вам_ прекрасно видно, почему утверждение,
    содержащее отрицание, выполнено, может понадобиться пара трюков, чтобы
    заставить Rocq это тоже понять!

    Вот доказательства нескольких понятных фактов для разогрева. *)

Theorem not_False :
  ~ False.
Proof.
  unfold not. intros H. destruct H. Qed.

Theorem contradiction_implies_anything : forall P Q : Prop,
  (P /\ ~P) -> Q.
Proof.
  (* РАЗБИРАЕМ НА ЛЕКЦИИ *) Admitted.

Theorem double_neg : forall P : Prop,
  P -> ~~P.
Proof.
  (* РАЗБИРАЕМ НА ЛЕКЦИИ *) Admitted.

(** Так как неравенство использует отрицание, комфортная работа с ним
    также требует небольшой предварительной практики.

    Полезный трюк: если вы пытаетесь доказать бредовую цель, примените
    [ex_falso_quodlibet], чтобы заменить текущую цель на [False]. Это упростит
    использование гипотез вида [~P], в том числе -- [x<>y]. *)

Theorem not_true_is_false : forall b : bool,
  b <> true -> b = false.
Proof.
  intros b H. destruct b eqn:HE.
  - (* b = true *)
    unfold not in H.
    apply ex_falso_quodlibet.
    apply H. reflexivity.
  - (* b = false *)
    reflexivity.
Qed.

