(** * Применение теорем к аргументам *)

(** В Rocq и _доказательства_ -- объекты первого класса! *)

(** Мы знаем, что можно использовать [Check], чтобы попросить Rocq проверить,
    имеет ли выражение заданный тип: *)

Check plus : nat -> nat -> nat.
Check @rev : forall X, list X -> list X.

(** Мы также можем использовать эту команду, чтобы проверить, к какой теореме
    относится данный идентификатор: *)

Check add_comm        : forall n m : nat, n + m = m + n.
Check plus_id_example : forall n m : nat, n = m -> n + n = m + m.

(** Rocq проверяет _утверждения_ теорем [add_comm] и [plus_id_example] таким же
    образом, каким он проверяет _тип_ любого терма (в т.ч. [plus]). Если мы
    опустим аннотации типов, Rocq распечатает их за нас.

    Почему? *)

(** Причина в том, что идентификатор [add_comm] на самом деле ссылается на
    _объект доказательства_ -- логический вывод, устанавливающий истинность
    утверждения [forall n m : nat, n + m = m + n].  Тип этого объекта --
    утверждение, для которого он является доказательством.

    Тип обычной функции сообщает нам, что с ней можно делать.
       - Если у нас есть терм типа [nat -> nat -> nat], мы можем передать ему
         два [nat]а в качестве аргументов и получить [nat] взамен.

    Похожим образом, утверждение теоремы сообщаем нам, для чего мы можем её
    использовать.
       - Если у нас есть терм типа [forall n m, n = m -> n + n = m + m] и мы
         передаём ему два числа [n] и [m], а также третий "аргумент" типа
         [n = m], мы получаем взамен объект доказательства типа [n + n = m + m].
*)

(** Rocq даже позволяет нам _применять_ теорему так, будто она функция.

    Это часто бывает удобно в скриптах доказательств -- пусть мы для примера
    хотим доказать следующее: *)

Lemma add_comm3 :
  forall x y z, x + (y + z) = (z + y) + x.

(** На первый взгляд кажется, что мы могли бы доказать это, дважды переписав
    по [add_comm], чтобы левая и правая сторона совпали.  Проблема в том, что
    второй вызов [rewrite] аннулирует результат первого. *)

Proof.
  intros x y z.
  rewrite add_comm.
  rewrite add_comm.
  (* Мы вернулись к тому, с чего начали... *)
Abort.

(** Мы можем исправить это, применяя [add_comm] к тем аргументам, которые
    нам нужны для инстанциирования.  Тогда [rewrite] по безысходности произойдёт
    там, где нам нужно. *)

Lemma add_comm3_take3 :
  forall x y z, x + (y + z) = (z + y) + x.
Proof.
  intros x y z.
  rewrite add_comm.
  rewrite (add_comm y z).
  reflexivity.
Qed.

(** И ещё один пример, теперь с использованием теоремы о списках в качестве
    функции.  Пусть мы доказали следующий простой факт о списках... *)

Theorem in_not_nil :
  forall A (x : A) (l : list A), In x l -> l <> [].
Proof.
  intros A x l H. unfold not. intro Hl.
  rewrite Hl in H.
  simpl in H.
  apply H.
Qed.

(** Обратите внимание, что подкванторная переменная [x] не появляется
    в заключении ([l <> []]). *)

(** Интуитивно, мы должны бы суметь использовать эту теорему для доказательства
    частного случая, в котором [x] это [42]. Однако обычное применение тактики
    [apply in_not_nil] провалится, поскольку оно не может вывести значение [x].
*)

Lemma in_not_nil_42 :
  forall l : list nat, In 42 l -> l <> [].
Proof.
  intros l H.
  Fail apply in_not_nil.
Abort.

(** Для обхода этой проблемы есть несколько способов: *)

(** Можем использовать [apply ... with ...]: *)
Lemma in_not_nil_42_take2 :
  forall l : list nat, In 42 l -> l <> [].
Proof.
  intros l H.
  apply in_not_nil with (x := 42).
  apply H.
Qed.

(** Или можно использовать [apply ... in ...]: *)
Lemma in_not_nil_42_take3 :
  forall l : list nat, In 42 l -> l <> [].
Proof.
  intros l H.
  apply in_not_nil in H.
  apply H.
Qed.

(** Или -- вот новый способ -- можем явно применить лемму с [42] вместо [x]: *)
Lemma in_not_nil_42_take4 :
  forall l : list nat, In 42 l -> l <> [].
Proof.
  intros l H.
  apply (in_not_nil nat 42).
  apply H.
Qed.

(** Также мы можем явно применить лемму к гипотезе, так что значения остальных
    параметров выведутся сами: *)
Lemma in_not_nil_42_take5 :
  forall l : list nat, In 42 l -> l <> [].
Proof.
  intros l H.
  apply (in_not_nil _ _ _ H).
Qed.

