(** ** Дизъюнкция *)

(** Другая важная связка -- это _дизъюнкция_, или _логическое ИЛИ_, двух
    утверждений: [A \/ B] истинно, когда какое-нибудь из [A] или [B] истинно.
    Инфиксной нотации соответствует [or A B], где [or : Prop -> Prop -> Prop].
*)

(** Чтобы использовать дизъюнкцию в доказательстве, нужно разобрать случаи --
    что, как и с другими типами данных вроде [nat], можно проделать явно
    с помощью [destruct], либо неявно с помощью образца в [intros]: *)

Lemma factor_is_O:
  forall n m : nat, n = 0 \/ m = 0 -> n * m = 0.
Proof.
  (* Этот паттерн неявно производит разбор случаев по [n = 0 \/ m = 0]... *)
  intros n m [Hn | Hm].
  - (* Здесь [n = 0] *)
    rewrite Hn. reflexivity.
  - (* Здесь [m = 0] *)
    rewrite Hm. rewrite <- mult_n_O.
    reflexivity.
Qed.

(** Соответственно, чтобы показать, что дизъюнкция верна, достаточно показать,
    что верно только одно из двух утверждений. Это можно сделать с помощью
    тактик [left] и [right].  Как подразумевают их имена, первая требует
    доказать левую часть дизъюнкции, а вторая -- правую.
    Вот тривиальный пример использования... *)

Lemma or_intro_l : forall A B : Prop, A -> A \/ B.
Proof.
  intros A B HA.
  left.
  apply HA.
Qed.

(** ... и чуть более интересный пример, где используется как [left], так и
    [right]: *)

