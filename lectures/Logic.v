(** ** Рекап -- логические связки в Rocq *)

(** Базовые связки:
       - [and : Prop -> Prop -> Prop] (конъюнкция):
         - вводится тактикой [split]
         - элиминируется через [destruct H as [H1 H2]]
       - [or : Prop -> Prop -> Prop] (дизъюнкция):
         - вводится тактикой [left] либо [right]
         - элиминируется через [destruct H as [H1 | H2]]
       - [False : Prop] (ложь)
         - элиминируется через [destruct H as []]
       - [True : Prop] (истина)
         - вводится через [apply I], но не так полезна
       - [ex : forall A:Type, (A -> Prop) -> Prop] (существование)
         - вводится через [exists w]
         - элиминируется через [destruct H as [x H]]

    Производные связки:
       - [not : Prop -> Prop] (отрицание):
         - [not P] определяется как [P -> False]
       - [iff : Prop -> Prop -> Prop] (логическая эквивалентность):
         - [iff P Q] определяется как [(P -> Q) /\ (Q -> P)]

    Фундаментальные связки, которые мы использовали с самого начала:
       - равенство ([e1 = e2])
       - импликация ([P -> Q])
       - всеобщность ([forall x, P]) *)

