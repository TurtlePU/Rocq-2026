(** ** Классическая и конструктивная логики *)

(** Следующий способ рассуждений не выводится в Rocq (однако, опять же,
    его можно непротиворечивым образом добавить в качестве аксиомы): *)

Definition excluded_middle := forall P : Prop,
  P \/ ~ P.

(** Логики вроде используемой в Rocq's, не предполагающие исключённое третье
    по умолчанию, называют _конструктивными логиками_.

    Логические системы вроде ZFC, в которых исключённое третье верно для
    произвольных утверждений, называют _классическими_. *)

