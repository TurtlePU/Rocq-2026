(** ** Сетоиды и логическая эквивалентность *)

(** Некоторые тактики Rocq работают с [iff] особенным образом, избегая некоторой
    низкоуровневой работы с состоянием доказательства.  В частности, [rewrite] и
    [reflexivity] можно использовать с [iff], не только с равенствами.
    Чтобы подключить это поведение, нам нужно подключить поддерживающую этот
    функционал библиотеку: *)
From Stdlib Require Import Setoids.Setoid.

(** "сетоид" -- это множество, снабжённое отношением эквивалентности,
    например [=] либо [<->]. *)

(** Ниже выписан простой пример, демонстрирующий, как эти тактики работают
    с [iff].

    Во-первых, давайте докажем несколько базовых эквивалентностей. (Для этих
    доказательств мы пока не используем сетоиды.) *)

Lemma mul_eq_0 : forall n m, n * m = 0 <-> n = 0 \/ m = 0.
Proof.
  split.
  - apply mult_is_O.
  - apply factor_is_O.
Qed.

Theorem or_assoc :
  forall P Q R : Prop, P \/ (Q \/ R) <-> (P \/ Q) \/ R.
Proof.
  intros P Q R. split.
  - intros [H | [H | H]].
    + left. left. apply H.
    + left. right. apply H.
    + right. apply H.
  - intros [[H | H] | H].
    + left. apply H.
    + right. left. apply H.
    + right. right. apply H.
Qed.

(** Теперь мы можем использовать эти факты вместе с [rewrite] и [reflexivity],
    чтобы доказать тернарную версию факта [mul_eq_0] выше _без_ разбиения
    верхнеуровневого [iff]: *)

Lemma mul_eq_0_ternary :
  forall n m p, n * m * p = 0 <-> n = 0 \/ m = 0 \/ p = 0.
Proof.
  intros n m p.
  rewrite mul_eq_0. rewrite mul_eq_0. rewrite or_assoc.
  reflexivity.
Qed.

