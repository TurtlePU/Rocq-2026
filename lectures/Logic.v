(** * Программирование утверждениями *)

(** Что значит "элемент [x] встречается в списке [l]"?

       - Если [l] -- пустой список, тогда [x] в нём встретиться не может,
         так что свойство "[x] встречается в [l]" попросту ложно.

       - В противном случае, [l] имеет вид [x' :: l'].  Так, [x] встречается
         в [l] только если он равен [x'] либо встречается в [l']. *)

(** Мы можем напрямую перевести эту формалировку в рекурсивную функцию,
    принимающую элемент и список и возвращающую... утверждение! *)

Fixpoint In {A : Type} (x : A) (l : list A) : Prop :=
  match l with
  | [] => False
  | x' :: l' => x' = x \/ In x l'
  end.

(** Когда [In] применяется к списку, оно разворачивается в конкретную
    последовательность вложенных дизъюнкций. *)

Example In_example_1 : In 4 [1; 2; 3; 4; 5].
Proof.
  (* РАЗБИРАЕМ НА ЛЕКЦИИ *) Admitted.

Example In_example_2 :
  forall n, In n [2; 4] ->
  exists n', n = 2 * n'.
Proof.
  (* РАЗБИРАЕМ НА ЛЕКЦИИ *) Admitted.

(** Также мы можем рассуждать о более общих утверждениях, содержащих [In]. *)

Theorem In_map :
  forall (A B : Type) (f : A -> B) (l : list A) (x : A),
         In x l ->
         In (f x) (map f l).
Proof.
  intros A B f l x.
  induction l as [|x' l' IHl'].
  - (* l = nil, противоречие *)
    simpl. intros [].
  - (* l = x' :: l' *)
    simpl. intros [H | H].
    + rewrite H. left. reflexivity.
    + right. apply IHl'. apply H.
Qed.

