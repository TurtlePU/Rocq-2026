(** Логические основания Rocq, _Исчисление индуктивных конструкций_, имеет
    несколько важных отличий от других формальных систем, используемых
    математиками для записи точных определений и доказательств -- в частности,
    от теории множеств Цермело-Френкеля (ZFC), наиболее популярных оснований
    "бумажной" математики.

    Мы завершаем эту главу кратким обсуждением некоторых наиболее важных отличий
    между этими мирами. *)

