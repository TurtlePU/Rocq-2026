(** * Работа с разрешимыми утверждениями *)

(** Мы видели два разных способа выражения логических утверждений в Rocq:
    _логическими значениями_ (типа [bool]), а также _утверждениями_
    (типа [Prop]).

    Ключевые отличия между [bool] и [Prop]:

                               bool     Prop
                               ====     ====
           разрешимо?          да        нет
           match?              да        нет
           переписывания?      нет       да
*)

(** Раз каждая функция на Rocq завершается на всех входах, функция типа
    [nat -> bool] -- это _процедура решения_, т.е. она возвращает [true] или
    [false] на всех входах.

      - Например, [even : nat -> bool] это процедура решения свойства чётности.
*)

(** Из этого следует, что есть такие свойства чисел, которые мы
    _не можем_ выразить как функции типа [nat -> bool].

      - Например, свойство "быть кодом завершающейся машины Тьюринга"
        неразрешимо, так что мы не можем записать её как функцию типа
        [nat -> bool].

    С другой стороны, [nat->Prop] это тип _всех_ свойств чисел, которые можно
    выразить в логике Rocq, включая как разрешимые, так и неразрешимые.

      - Например, "быть кодом завершающейся машины Тьюринга" -- совершенно
        корректное математическое свойство, и мы спокойно можем представить его
        в виде выражения типа [nat -> Prop].
*)

(** Раз [Prop] включает как разрешимые, так и неразрешимые свойства, у нас
    появляется два варианта, когда мы хотим формализовать разрешимое свойство:
    можно выразить его и как вычисление на логических значениях, и как функцию
    в [Prop]. *)

(** Например, чтобы утверждать, что [n] чётно, мы можем как сказать, что
    [even n] вычисляется в [true]... *)
Example even_42_bool : even 42 = true.
Proof. reflexivity. Qed.

(** ... так и что существует [k] такое, что [n = double k]. *)
Example even_42_prop : Even 42.
Proof. unfold Even. exists 21. reflexivity. Qed.

(** Конечно, было бы очень странно, если бы эти две характеристики чётности не
    описывали одно и то же множество натуральных чисел.

    К счастью, описывают! *)

(** Чтобы доказать это, нам нужны две вспомогательные леммы. *)

Lemma even_double : forall k, even (double k) = true.
Proof.
  intros k. induction k as [|k' IHk'].
  - reflexivity.
  - simpl. apply IHk'.
Qed.

Lemma even_double_conv : forall n, exists k,
  n = if even n then double k else S (double k).
Proof.
  (* Подсказка: Используйте лемму [even_S] из [Induction.v]. *)
  (* ЗАПОЛНИТЕ ЗДЕСЬ *) Admitted.
(** [] *)

(** Теперь перейдём к главной теореме: *)

Theorem even_bool_prop : forall n,
  even n = true <-> Even n.
Proof.
  intros n. split.
  - intros H. destruct (even_double_conv n) as [k Hk].
    rewrite Hk. rewrite H. exists k. reflexivity.
  - intros [k Hk]. rewrite Hk. apply even_double.
Qed.

(** С учётом этой теоремы, мы можем сказать, что логическое вычисление [even n]
    _отражено_ в истинности утверждения [exists k, n = double k]. *)

(** Похожим образом, утверждать равенство двух чисел [n] и [m] можно двумя
    способами:
      - (1) что [n =? m] возвращает [true], или
      - (2) что [n = m].
    И вновь эти два утверждения эквивалентны: *)

Theorem eqb_eq : forall n1 n2 : nat,
  n1 =? n2 = true <-> n1 = n2.
Proof.
  intros n1 n2. split.
  - apply eqb_true.
  - intros H. rewrite H. rewrite eqb_refl. reflexivity.
Qed.

(** И что же мы должны делать в случаях, когда некоторое утверждение можно
    формализовать и как утверждение, и как вычисление? Что нам выбрать?

    В общем случая, _оба_ представления полезны. *)

(** Например, логические значения более полезны при определении функций. Нельзя
    эффективно _проверить_, истинен ли некоторый [Prop], так что мы не можем
    использовать [Prop] в условных выражениях. Следующее определение будет
    отвергнуто: *)

Fail
Definition is_even_prime n :=
  if n = 2 then true
  else false.

(** Вместо этого нам нужно определить эту функцию через проверку на равенство.
*)

Definition is_even_prime n :=
  if n =? 2 then true
  else false.

(** В более общем случае, выписывание фактов через логические значения
    зачастую позволяет автоматизировать доказательства с помощью вычислений
    на термах; эта техника известна как _доказательство рефлексией_.

    Рассмотрим следующее утверждение: *)

Example even_1000 : Even 1000.

(** Самый прямолинейный способ доказательства -- явно указать значение [k]. *)

Proof. unfold Even. exists 500. reflexivity. Qed.

(** Доказательство соответствующего утверждения о логических значениях проще,
    поскольку нам не нужно искать значение свидетеля: механизм вычислений Rocq
    делает это за нас! *)

Example even_1000' : even 1000 = true.
Proof. reflexivity. Qed.

(** Теперь, полезно заметить, что раз эти два понятия эквивалентны, мы можем
    использовать формулировку через логические значения, чтобы доказать
    утверждение, не упоминая 500 явно: *)

Example even_1000'' : Even 1000.
Proof. apply even_bool_prop. reflexivity. Qed.

(** Хоть мы и не особо выиграли по числу строчек, доказательства большего
    размера можно существенно упростить с помощью рефлексии.  Так, знаменитое
    доказательство _теоремы о четырёх красках_ на Rocq использует рефлексию,
    чтобы свести анализ сотен различных случаев к логическим вычислениям. *)

(** Другое достоинство логических значений в том, что _отрицание_ утверждений
    о логических значениях проще как формулировать, так и доказывать: просто
    проотрицаем ожидаемый результат вычислений. *)

Example not_even_1001 : even 1001 = false.
Proof.
