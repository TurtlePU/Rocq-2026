(** * Улучшаем рефлексию *)

(** Мы видели, что нам часто нужно связывать булевы вычисления с утверждениями
    в [Prop]: *)

Check eqb_eq : forall n1 n2, (n1 =? n2) = true <-> n1 = n2.

(** Однако это может приводить к рутине в скриптах доказательств. Рассмотрим: *)

Theorem filter_not_empty_In : forall n l,
  filter (fun x => n =? x) l <> [] -> In n l.
Proof.
  intros n l. induction l as [|m l' IHl'].
  - (* l = nil *)
    simpl. intros H. apply H. reflexivity.
  - (* l = m :: l' *)
    simpl. destruct (n =? m) eqn:H.
    + (* n =? m = true *)
      intros _. rewrite eqb_eq in H. rewrite H.
      left. reflexivity.
    + (* n =? m = false *)
      intros H'. right. apply IHl'. apply H'.
Qed.

(** Первая подцель (где [n =? m = true]) выглядит неуклюже, поскольку нам
    нужно явно "переключаться между мирами".

    Было бы неприятно всё время повторять эти выкладки. *)

(** Мы можем упростить этот ход рассуждений, определив индуктивное утверждение,
    которое даёт улучшенный способ разбора случаев для [n =? m].  Вместо того,
    чтобы генерировать гипотезу [(n =? m) = true], которое требует некоторого
    причёсывания перед использованием, улучшенный способ сразу даст нужное нам
    утверждение: [n = m].

    Следуя терминологии, введённой в [Logic], мы называем это "принципом
    рефлексии на равенстве чисел", и мы говорим, что булево значение [n =? m]
    _отражено_ в утверждении [n = m]. *)

Inductive reflect (P : Prop) : bool -> Prop :=
  | ReflectT (H :   P) : reflect P true
  | ReflectF (H : ~ P) : reflect P false.

(** Заметьте, что единственный способ произвести свидетельство для
    [reflect P true] -- показать [P] и использовать конструктор [ReflectT].

    Если мы применим [inversion], то мы можем извлечь _свидетеля_ [P]
    из свидетельства [reflect P true]. *)

(** Чтобы использовать это наблюдение, для начала докажем, что утверждения
    [P <-> b = true] и [reflect P b] действительно эквивалентны.  Для начала
    докажем импликацию слева направо: *)

Theorem iff_reflect : forall P b, (P <-> b = true) -> reflect P b.
Proof.
  (* РАЗБИРАЕМ НА ЛЕКЦИИ *) Admitted.

(** (Следствие справа налево оставлено в качестве упражнения.) *)

(** Мы можем думать о [reflect] как о разновидности связки "если и только если";
    преимущество [reflect] в том, что, разбирая случаи по [reflect P b], мы
    можем производить разбор случаев по [b], _в тот же самый момент_ генерируя
    подходящие гипотезы в случаях ([P] в первой подцели, [~ P] во второй). *)

(** Давайте используем [reflect], чтобы произвести более красивое доказательство
    [filter_not_empty_In].

    Начнём с перевода леммы [eqb_eq] в терминах [reflect]: *)

Lemma eqbP : forall n m, reflect (n = m) (n =? m).
Proof.
  intros n m. apply iff_reflect. rewrite eqb_eq. reflexivity.
Qed.

(** Доказательство [filter_not_empty_In] теперь продвигается следующим образом.
    Обратите внимание, как вызовы [destruct] и [rewrite] в раннем доказательстве
    этой теоремы совмещены в один вызов [destruct]. *)

Theorem filter_not_empty_In' : forall n l,
  filter (fun x => n =? x) l <> [] ->
  In n l.
Proof.
  intros n l. induction l as [|m l' IHl'].
  - (* l = [] *)
    simpl. intros H. apply H. reflexivity.
  - (* l = m :: l' *)
    simpl. destruct (eqbP n m) as [EQnm | NEQnm].
    + (* n = m *)
      intros _. rewrite EQnm. left. reflexivity.
    + (* n <> m *)
      intros H'. right. apply IHl'. apply H'.
Qed.

(** Этот небольшой пример демонстрирует, как рефлексия делает доказательства
    немного более удобными; консистентное использование [reflect] в более
    сложных проектах часто приводит к гораздо более простым и понятным скриптам
    доказательств. Мы увидим больше примеров в следующих занятиях.

    Такой способ использования [reflect] был популяризован библиотекой
    _SSReflect_, которая была использована для формализации важных результатов в
    математике, включая теорему о четырёх красках и теорему Фейта-Томпсона.
    Название SSReflect расшифровывается как _small-scale reflection_, т.е.
    систематическое использование рефлексии для замены рутинных шагов
    в доказательствах на булевы вычисления. *)

