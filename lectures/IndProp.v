(** ** Индукция по свидетельству *)

(** То, что эта история звучит знакомо, не совпадение: мы сталкивались
    с похожими проблемами в главе [Induction], когда пытались доказывать
    теоремы разбором случаев, когда нам была нужна индукция.  А решение... снова
    индукция! *)

(** Вновь попробуем доказать эту лемму: *)

Lemma ev_Even : forall n,
  ev n -> Even n.
Proof.
  unfold Even. intros n E.
  induction E as [|n' E' IH].
  - (* E = ev_0 *)
    exists 0. reflexivity.
  - (* E = ev_SS n' E',  with IH : Even n' *)
    destruct IH as [k Hk]. rewrite Hk.
    exists (S k). simpl. reflexivity.
Qed.

