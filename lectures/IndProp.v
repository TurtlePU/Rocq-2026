(** ** Построение свидетельств перестановки *)

(** Похожим образом, мы можем применять конструкторы свидетельств, чтобы
    получить свидетельство [Perm3 [1;2;3] [3;2;1]]: *)

Lemma Perm3_rev : Perm3 [1;2;3] [3;2;1].
Proof.
  apply perm3_trans with (l2:=[2;3;1]).
  - apply perm3_trans with (l2:=[2;1;3]).
    + apply perm3_swap12.
    + apply perm3_swap23.
  - apply perm3_swap12.
Qed.

(** И, опять же, мы можем использовать синтаксис применения функций, чтобы
    скомбинировать несколько конструкторов. (Обратите внимание, что проверка
    типов в Rocq выводит не только типы, но и значения, когда они понятны
    из контекста.) *)

Lemma Perm3_rev' : Perm3 [1;2;3] [3;2;1].
Proof.
  apply (perm3_trans _ [2;3;1] _
          (perm3_trans _ [2;1;3] _
            (perm3_swap12 _ _ _)
            (perm3_swap23 _ _ _))
          (perm3_swap12 _ _ _)).
Qed.

(** Так, деревья вывода, которые мы выписывали выше, очень похожи на то,
    что реально происходит в Rocq.  Формально говоря, мы используем конструкторы
    свидетельств для построения _деревьев вывода_, похожих на конечные деревья,
    которые мы строили, используя конструкторы типов данных вроде nat, list,
    бинарных деревьев и т.д. *)

(* ################################################################# *)
(** * Использование свидетельств в доказательствах *)

(** Помимо _построения_ свидетельств чётности чисел, мы также можем _разбирать
    случаи_ по таким свидетельствам, рассуждая о том, как они могли быть
    построены.

    Определение [ev] через [Inductive] говорить Rocq не только то, что
    конструкторы [ev_0] и [ev_SS] -- корректные способы построения свидетельства
    [ev], но и то, что эти два конструктора -- _единственные_ такие способы. *)

(** Другими словами, если кто-то предъявляет нам свидетельство [E] утверждения
    [ev n], то мы знаем, что [E] устроено одним из двух способов:

      - [E = ev_0] и [n = O], либо
      - [E = ev_SS n' E'] и [n = S (S n')], где [E' : ev n']. *)

(** Это наводит на мысль, что, можем быть, по таким свидетельствам можно
    _разбирать случаи_, или даже запускать _индукцию_... *)

(* ================================================================= *)
(** ** Разбор и инверсия свидетельств *)

(** Мы можем доказать нашу характеризацию [ev n], используя [destruct].
*)

Lemma ev_inversion : forall (n : nat),
    ev n ->
    (n = 0) \/ (exists n', n = S (S n') /\ ev n').
Proof.
  intros n E.  destruct E as [ | n' E'] eqn:EE.
  - (* E = ev_0 : ev 0 *)
    left. reflexivity.
  - (* E = ev_SS n' E' : ev (S (S n')) *)
    right. exists n'. split. reflexivity. apply E'.
Qed.

(** Факты вроде этого часто называют "леммами об инверсии", поскольку они
    позволяют нам как бы "развернуть вспять" некоторую информацию, чтобы
    рассуждать о всевозможных способах того, каким образом эта информация могла
    быть получена. *)

(** Мы можем использовать лемму об инверсии, которую мы доказали выше, чтобы
    лучше структурировать доказательства: *)

Theorem evSS_ev : forall n, ev (S (S n)) -> ev n.
Proof.
  intros n E. apply ev_inversion in E. destruct E as [H0|H1].
  - discriminate H0.
  - destruct H1 as [n' [Hnn' E']]. injection Hnn' as Hnn'.
    rewrite Hnn'. apply E'.
Qed.

(** Rocq предоставляет тактику [inversion], применяющую лемму об инверсии
    и убирающую неприменимые случаи. *)

Theorem evSS_ev' : forall n,
  ev (S (S n)) -> ev n.
Proof.
  intros n E.  inversion E as [| n' E' Hnn'].
  (* Здесь мы уже разбираем случай [E = ev_SS n' E']. *)
  apply E'.
Qed.

(** Мы можем использовать [inversion], чтобы заново доказать некоторые
    теоремы из [Tactics.v].

    Обратите внимание, что [inversion] работает и на равенствах. *)

Theorem inversion_ex1 : forall (n m o : nat),
  [n; m] = [o; o] -> [n] = [m].
Proof.
  intros n m o H. inversion H. reflexivity. Qed.

Theorem inversion_ex2 : forall (n : nat),
  S n = O -> 2 + 2 = 5.
Proof.
  intros n contra. inversion contra. Qed.

(** Тактика [inversion] работает на любом [H : P], где [P] определено
    [Индуктивно]:

      - Создаёт по подцели на каждый конструктор [P], где [H] ограничено видом
