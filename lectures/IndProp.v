(** ** Пример: Транзитивное замыкание *)

(** Другой пример: _транзитивное замыкание_ отношения [R] -- это наименьшее
    отношение, содержащее [R], являющееся транзитивным. Его можно задать
    следующими правилами:

                     R x y
                ---------------- (t_step)
                clos_trans R x y

       clos_trans R x y    clos_trans R y z
       ------------------------------------ (t_trans)
                clos_trans R x z

    В Rocq это выглядит вот так:
*)

Inductive clos_trans {X: Type} (R: X->X->Prop) : X->X->Prop :=
  | t_step (x y : X) :
      R x y ->
      clos_trans R x y
  | t_trans (x y z : X) :
      clos_trans R x y ->
      clos_trans R y z ->
      clos_trans R x z.

(** Так, пусть у нас определено отношение "является родителем"... *)

Inductive Person : Type := Sage | Cleo | Ridley | Moss.

Inductive parent_of : Person -> Person -> Prop :=
  po_SC : parent_of Sage Cleo
| po_SR : parent_of Sage Ridley
| po_CM : parent_of Cleo Moss.

(** Отношение [parent_of] не транзитивно, но мы можем определить отношение
    "быть предком для" как транзитивное замыкание этого отношения: *)

Definition ancestor_of : Person -> Person -> Prop :=
  clos_trans parent_of.

(** Вот дерево вывода, демонстрирующее, что Sage -- предок Moss:

 ———————————————————(po_SC)     ———————————————————(po_CM)
 parent_of Sage Cleo            parent_of Cleo Moss
—————————————————————(t_step)  —————————————————————(t_step)
ancestor_of Sage Cleo          ancestor_of Cleo Moss
————————————————————————————————————————————————————(t_trans)
                ancestor_of Sage Moss
*)

Example ancestor_of_ex : ancestor_of Sage Moss.
Proof.
  unfold ancestor_of. apply t_trans with Cleo.
  - apply t_step. apply po_SC.
  - apply t_step. apply po_CM. Qed.

(* ================================================================= *)
(** ** Пример: Рефлексивно-транзитивное замыкание *)

(** Вот ещё один пример: _рефлексивно-транзитивное замыкание_ отношения [R] --
    наименьшее отношение, содержащее [R], которое и рефлексивно, и транзитивно.
    Его можно построить с помощью следующих трёх правил (в отличие от
    [clos_trans], мы добавили правило, постулирующее рефлексивность):

                        R x y
                --------------------- (rt_step)
                clos_refl_trans R x y

                --------------------- (rt_refl)
                clos_refl_trans R x x

     clos_refl_trans R x y    clos_refl_trans R y z
     ---------------------------------------------- (rt_trans)
                clos_refl_trans R x z
*)

Inductive clos_refl_trans {X: Type} (R: X->X->Prop) : X->X->Prop :=
  | rt_step (x y : X) :
      R x y ->
      clos_refl_trans R x y
  | rt_refl (x : X) :
      clos_refl_trans R x x
  | rt_trans (x y z : X) :
      clos_refl_trans R x y ->
      clos_refl_trans R y z ->
      clos_refl_trans R x z.

(** Кстати, это позволяет нам дать эквивалентное определение гипотезы Коллатца.
    Сначала выпишем функцию шага Коллатца в виде отношения: *)

Definition cs (n m : nat) : Prop := csf n = m.

(** Это "отношение шага Коллатца" можно подставить в рефлексивно-транзитивное
    замыкание, чтобы получить _многошаговое отношение Коллатца_, выражающее, что
    число [n] достигает другого числа [m] за некоторое число шагов Коллатца: *)

Definition cms n m := clos_refl_trans cs n m.
Conjecture collatz' : forall n, n <> 0 -> cms n 1.

