(** ** Пример: Чётность (снова) *)

(** Мы видели как минимум два способа формализации понятия чётности:

      (1) [even n = true] (используя рекурсивную функцию [even]), или

      (2) [exists k, n = double k] (используя квантор существования). *)

(** Третий способ, который мы будем использовать в качестве подопытного кролика
    в этой главе, это сказать, что число чётное, если мы можем _установить_ его
    чётность, пользуясь следующими двумя правилами вывода:

                          ---- (ev_0)
                          ev 0

                          ev n
                      ------------ (ev_SS)
                      ev (S (S n))
*)

(** Вот как с помощью этих правил доказывать чётность четырёх:

                           ———— (ev_0)
                           ev 0
                       ———————————— (ev_SS)
                       ev (S (S 0))
                   ———————————————————— (ev_SS)
                   ev (S (S (S (S 0))))
*)

(** Мы можем перевести "бумажное" определение чётности выше в [Индуктивное]
    объявление, где каждая "возможность числа оказаться чётным" соответствует
    своему собственному конструктору: *)

Inductive ev : nat -> Prop :=
  | ev_0                       : ev 0
  | ev_SS (n : nat) (H : ev n) : ev (S (S n)).

(** Есть как сходства, так и различия между индуктивными _свойствами_
    вроде [ev] и индуктивными _типами_ вроде [nat] или [list]:

    Inductive list (X:Type) : Type :=
      | nil                       : list X
      | cons (x : X) (l : list X) : list X.

    Самая важная разница состоит в том, что конструкторы [ev], [ev_0] и [ev_SS],
    возвращают значения разных типов ([ev 0] и [ev (S (S n))]), когда как
    конструкторы [list] всегда возвращают значения типа [list X]. *)

(** Про определение [ev] можно думать как про определение свойства
    [ev : nat -> Prop] вместе с двумя "конструкторами свидетельств": *)

Check ev_0 : ev 0.
Check ev_SS : forall (n : nat), ev n -> ev (S (S n)).

(** Про эти конструкторы можно думать как про "простейшие свидетельства
    чётности", которыми можно пользоваться как доказанными теоремами.
    В частности, мы можем использовать тактику [apply] с этими конструкторами,
    чтобы получить свидетельства чётности конкретных чисел... *)

Theorem ev_4 : ev 4.
Proof. apply ev_SS. apply ev_SS. apply ev_0. Qed.

(** ... и использовать синтаксис применения функций, чтобы применить сразу
    несколько конструкторов: *)

Theorem ev_4' : ev 4.
Proof. apply (ev_SS 2 (ev_SS 0 ev_0)). Qed.

(** Таким образом, мы можем доказывать теоремы с гипотезами,
    включающими [ev]. *)

Theorem ev_plus4 : forall n, ev n -> ev (4 + n).
Proof.
  intros n. simpl. intros Hn.  apply ev_SS. apply ev_SS. apply Hn.
Qed.

